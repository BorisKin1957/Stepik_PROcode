'''Шаг 1. Объявите функцию-декоратор add_starting_balance(start),
которая принимает число start и возвращает декоратор.
Декоратор должен прибавлять start к результату декорируемой функции.
Шаг 2. Объявите функцию calculate_expenses(data), которая принимает строку из целых чисел,
разделённых пробелами, преобразует её в список чисел и возвращает их сумму.
Шаг 3. Примените декоратор к calculate_expenses со значением start=5
(используйте @add_starting_balance(5)).'''

# Определение фабрики декораторов: функция, которая принимает аргумент start
def add_starting_balance(start):
    # Внутренняя функция — сам декоратор, принимает функцию func, которую будем оборачивать
    def outer(func):
        # Внутренняя функция-обёртка, которая заменит оригинальную функцию
        def inner(num):
            # Выполняем оригинальную функцию с переданным аргументом num
            # и прибавляем к результату значение start из внешней области видимости
            return func(num) + start
        # Возвращаем обёрнутую функцию (не вызываем!)
        return inner
    # Возвращаем декоратор outer (ожидает функцию для декорирования)
    return outer


# Применяем декоратор @add_starting_balance(5) к функции ниже
# Это эквивалентно: calculate_expenses = add_starting_balance(5)(calculate_expenses)
@add_starting_balance(5)
def calculate_expenses(data: str) -> int:
    # Преобразуем входную строку data в список чисел:
    # разбиваем строку по пробелам, каждую подстроку преобразуем в int
    # затем суммируем все числа и возвращаем результат
    return sum([int(num) for num in data.split()])


# Считываем строку с клавиатуры (например, "10 20 30"), передаём в функцию
# и выводим результат (сумма чисел + 5 благодаря декоратору)
