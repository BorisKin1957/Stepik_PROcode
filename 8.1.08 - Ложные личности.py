'''✅ Получить словарь (ключ = ник, значение = IP).
✅ Удалить пробелы в IP.
✅ Найти повторяющийся IP-адрес (который используют несколько никнеймов).
✅ Вывести никнеймы и IP-адрес Призрака.
✅ Если все IP уникальны --> вывести "Призрак не найден"

Примечание: Не используйте set()!'''

from json import loads

# Считываем входные данные в формате JSON и преобразуем в словарь
data = loads(input())

# Создаём новый словарь, где ключ — очищенный IP, значение — список ников, использующих этот IP
new = {}

# Проходим по исходному словарю: nic — никнейм, ip — IP-адрес
for nic, ip in data.items():
    # Удаляем пробелы в IP и добавляем ник в список для этого IP
    # Если такого IP ещё нет в словаре, создаётся пустой список
    new.setdefault(ip.strip(), []).append(nic)

# Фильтруем словарь: оставляем только те записи, где более одного ника используют один IP
result = dict(filter(lambda x: len(x[1]) > 1, new.items()))

# Если нет совпадающих IP — призрака нет
if not result:
    print('Призрак не найден')
else:
    # Для первого найденного совпадения выводим сообщение
    for ip, nic in result.items():
        print(f'Настоящий хакер: {", ".join(nic)} (IP: {ip})')
        break


