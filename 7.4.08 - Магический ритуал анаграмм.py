'''На вход подаются два слова, по одному на каждой строке.
Определите, являются ли они анаграммами (состоят из одинаковых букв в любом порядке).
Выведите ДА, если являются, иначе --> НЕТ.
Не забудьте привести к одному регистру'''


# Считываем первое слово и переводим его в нижний регистр
word = input().lower()
# Создаём словарь, где ключ — буква, значение — количество её вхождений в слове
letters_1 = {letter: word.count(letter) for letter in word}

# Считываем второе слово и переводим его в нижний регистр
word = input().lower()
# Аналогично создаём словарь для второго слова
letters_2 = {letter: word.count(letter) for letter in word}

# Сравниваем два словаря: если совпадают, значит слова — анаграммы
print('ДА' if letters_1 == letters_2 else 'НЕТ')