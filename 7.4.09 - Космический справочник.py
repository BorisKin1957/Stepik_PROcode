'''Считайте целое n число записей.

Далее в n строках даны пары: телефонный номер (последовательность цифр) и имя
(одно слово буквами русского или английского алфавита).

Затем считайте целое m  число запросов. В следующих m строках даны имена для поиска.

Для каждого запроса выведите в отдельной строке все номера, принадлежащие
человеку с заданным именем (поиск без учёта регистра), в исходном порядке,
разделённые пробелом.

Если контакта с таким именем нет, выведите строку абонент не найден.'''

# Создаём словарь для хранения имён (в нижнем регистре) и списка номеров
requests = {}

# Считываем количество записей
for _ in range(int(input())):
    # Считываем строку, разбиваем на номер и имя, оба приводим к нижнему регистру
    number, name = input().lower().split()
    # Добавляем номер к списку номеров для данного имени; если имени ещё нет —
    # создаём пустой список
    requests.setdefault(name, []).append(number)

# Считываем количество запросов
for _ in range(int(input())):
    # Считываем имя для поиска и приводим к нижнему регистру
    name = input().lower()
    # Если такое имя есть в справочнике
    if name in requests:
        # Выводим все номера, принадлежащие этому имени, через пробел
        print(*requests[name])
    else:
        # Если имя не найдено — выводим сообщение
        print('абонент не найден')

