# –°—á–∏—Ç—ã–≤–∞–µ–º –¥–≤–∞ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞: –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–æ–Ω–µ—Ç –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–∏—Ä–∞—Ç–æ–≤
# –ö–∞–∂–¥–æ–µ —á–∏—Å–ª–æ –≤–≤–æ–¥–∏—Ç—Å—è —Å –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏
coins, corsairs = int(input()), int(input())

# –í—ã—á–∏—Å–ª—è–µ–º —Ä–∞—Å—Ö–æ–¥—ã –Ω–∞ —Ä–µ–º–æ–Ω—Ç ‚Äî —ç—Ç–æ 10% –æ—Ç –æ–±—â–µ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –º–æ–Ω–µ—Ç
# –¢–∞–∫ –∫–∞–∫ –º–æ–Ω–µ—Ç—ã ‚Äî —Ü–µ–ª—ã–µ, –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–µ –¥–µ–ª–µ–Ω–∏–µ
refit = coins // 10

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º, —Å–∫–æ–ª—å–∫–æ –º–æ–Ω–µ—Ç –¥–æ—Å—Ç–∞–Ω–µ—Ç—Å—è –∫–∞–∂–¥–æ–º—É –ø–∏—Ä–∞—Ç—É
# –°–Ω–∞—á–∞–ª–∞ –≤—ã—á–∏—Ç–∞–µ–º —Ä–∞—Å—Ö–æ–¥—ã (refit), –∑–∞—Ç–µ–º –¥–µ–ª–∏–º –æ—Å—Ç–∞—Ç–æ–∫ –ø–æ—Ä–æ–≤–Ω—É –º–µ–∂–¥—É –ø–∏—Ä–∞—Ç–∞–º–∏
pay = (coins - refit) // corsairs

# –í—ã—á–∏—Å–ª—è–µ–º, —Å–∫–æ–ª—å–∫–æ –º–æ–Ω–µ—Ç –æ—Å—Ç–∞–Ω–µ—Ç—Å—è –ø–æ—Å–ª–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è
# –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–∏–Ω—É—Å —Ä–∞—Å—Ö–æ–¥—ã –º–∏–Ω—É—Å —Ç–æ, —á—Ç–æ —Ä–∞–∑–¥–∞–ª–∏ –ø–∏—Ä–∞—Ç–∞–º
# pay * corsairs ‚Äî —ç—Ç–æ —Å–∫–æ–ª—å–∫–æ –≤—Å–µ–≥–æ –º–æ–Ω–µ—Ç –ø–æ–ª—É—á–∏–ª–∏ –ø–∏—Ä–∞—Ç—ã
remain = coins - refit - pay * corsairs

# –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –≤ —Å—Ç—Ä–æ–≥–æ –∑–∞–¥–∞–Ω–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ
print(f'–ú–æ–Ω–µ—Ç—ã –Ω–∞ —Ä–∞—Å—Ö–æ–¥—ã: {refit}')
print(f'–ö–∞–∂–¥—ã–π –ø–∏—Ä–∞—Ç –ø–æ–ª—É—á–∏—Ç {pay} –º–æ–Ω–µ—Ç.')
print(f'–û—Å—Ç–∞–Ω–µ—Ç—Å—è {remain} –º–æ–Ω–µ—Ç.')
