'''Шаг 1. Объявите функцию count_messages(thread) -> int, где thread --> список, элементы которого:
— обычные сообщения как строки (str);
— списки-ветки с ответами, которые могут содержать строки и/или новые вложенные списки.

Шаг 2. Функция должна рекурсивно обойти структуру любой глубины и вернуть общее количество
сообщений (строк).
Строка считается одним сообщением. Пустые списки дают вклад 0.'''


def count_messages(thread: list[str, list[str]]) -> int:
    '''Рекурсивная функция для подсчёта всех строковых сообщений в древовидной структуре.

    Аргумент:
        thread (list): список, содержащий строки или вложенные списки строк и списков.

    Возвращает:
        int: общее количество строк (сообщений) во всей структуре.
    '''
    # Базовый случай: если список пуст, возвращаем 0
    if not thread:
        return 0

    # Проверяем тип первого элемента списка
    if type(thread[0]) is str:
        # Если первый элемент — строка, значит это одно сообщение
        # Добавляем 1 и продолжаем подсчёт для оставшихся элементов
        return 1 + count_messages(thread[1:])
    else:
        # Если первый элемент — список (ветка с ответами),
        # рекурсивно считаем сообщения внутри этого списка
        # и добавляем к результату подсчёта остальной части основного списка
        return count_messages(thread[0]) + count_messages(thread[1:])


# Пример 1: тестовая структура сообщений
messages = [
    "Привет!",  # обычное сообщение
    [  # ветка с ответами
        "Как дела?",  # сообщение внутри ветки
        ["Хорошо, а у тебя?",  # вложенная ветка
         ["Тоже отлично!"]  # ещё более глубокая вложенность
         ]
    ],
    "Что нового?"  # ещё одно обычное сообщение
]

# Выводим общее количество сообщений в структуре
print(count_messages(messages))  # Ожидаемый результат: 5

# Пример 2: более сложная структура
messages = [
    "Начинаем собрание",  # сообщение
    ["Кто присутствует?",  # ветка
     ["Я", "Я тоже"]  # ответы
     ],
    "Переходим к вопросам",  # сообщение
    ["Когда дедлайн?",  # ветка
     ["Через неделю",  # ответ
      ["А можно отложить?",  # вложенная ветка
       ["Нет!"]  # финальный ответ
       ]
      ]
     ]
]

# Выводим результат для второго примера
print(count_messages(messages))  # Ожидаемый результат: 8