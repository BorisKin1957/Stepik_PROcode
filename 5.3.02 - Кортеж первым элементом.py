'''Считай одну строку с вложенными кортежами, записанными через пробел, например:

(1, 2) (10, 20) (5, 15)

Определи:

    кортеж с минимальным первым элементом;

    кортеж с максимальным первым элементом.

Ограничение: не используйте sorted, list, set, dict, range, eval

Выведи две строки строго в таком виде:

Кортеж с минимальным первым элементом: (<кортеж>)
Кортеж с максимальным первым элементом: (<кортеж>)'''

# Считываем одну строку с вложенными кортежами, например: (1, 2) (10, 20) (5, 15)
string = input()

# Создаём пустой список, в который будем добавлять кортежи
tuples = []

# Убираем все скобки и запятые из строки, чтобы оставить только числа, разделённые пробелами
string = string.replace('(', '').replace(')', '').replace(',', '')

# Разбиваем строку на отдельные числа, преобразуем их в целые числа и сохраняем в список
numbers = [int(num) for num in string.split()]

i = 0
# Проходим по списку чисел с шагом 2, чтобы брать пары чисел (первый и второй элемент кортежа)
while i < len(numbers):
    tmp = []  # Временный список для хранения двух чисел
    tmp.append(numbers[i])      # Добавляем первое число пары
    tmp.append(numbers[i + 1])  # Добавляем второе число пары
    tuples.append(tuple(tmp))   # Преобразуем список в кортеж и добавляем в общий список кортежей
    i += 2

# Находим кортеж с минимальным первым элементом, используя min и ключ по первому элементу кортежа
print(f'Кортеж с минимальным первым элементом: {min(tuples, key=lambda x: x[0])}')

# Находим кортеж с максимальным первым элементом, используя max и ключ по первому элементу кортежа
print(f'Кортеж с максимальным первым элементом: {max(tuples, key=lambda x: x[0])}')
