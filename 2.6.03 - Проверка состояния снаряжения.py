# –°—á–∏—Ç—ã–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É —Å–æ —Å–ø–∏—Å–∫–æ–º –ø—Ä–µ–¥–º–µ—Ç–æ–≤, —Ä–∞–∑–¥–µ–ª—ë–Ω–Ω—ã—Ö –∑–∞–ø—è—Ç—ã–º–∏, –∏ —Ä–∞–∑–±–∏–≤–∞–µ–º –µ—ë –Ω–∞ —Å–ø–∏—Å–æ–∫ –ø–æ –∑–∞–ø—è—Ç–æ–π —Å –ø—Ä–æ–±–µ–ª–æ–º
kit = input().split(', ')

# –°–æ–∑–¥–∞—ë–º –¥–≤–∞ –ø—É—Å—Ç—ã—Ö —Å–ø–∏—Å–∫–∞: –¥–ª—è —Ü–µ–ª–æ–≥–æ (good_arm) –∏ –ø–æ–≤—Ä–µ–∂–¥—ë–Ω–Ω–æ–≥–æ (bad_arm) —Å–Ω–∞—Ä—è–∂–µ–Ω–∏—è
good_arm, bad_arm = [], []

# –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç (–ø—Ä–µ–¥–º–µ—Ç) –∏–∑ –≤–≤–µ–¥—ë–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞
for item in kit:
    # –ï—Å–ª–∏ —É –ø—Ä–µ–¥–º–µ—Ç–∞ –≤ –∫–æ–Ω—Ü–µ —Å—Ç–æ–∏—Ç —Å–ª–æ–≤–æ "–ø–æ–≤—Ä–µ–∂–¥—ë–Ω" (–ø–æ—Å–ª–µ–¥–Ω–∏–µ 9 —Å–∏–º–≤–æ–ª–æ–≤ —Å—Ç—Ä–æ–∫–∏)
    if item[-9:] == '–ø–æ–≤—Ä–µ–∂–¥—ë–Ω':
        # –î–æ–±–∞–≤–ª—è–µ–º –≤ —Å–ø–∏—Å–æ–∫ –ø–æ–≤—Ä–µ–∂–¥—ë–Ω–Ω–æ–≥–æ —Å–Ω–∞—Ä—è–∂–µ–Ω–∏—è –≤—Å—ë, –∫—Ä–æ–º–µ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö 10 —Å–∏–º–≤–æ–ª–æ–≤ (—É–±–∏—Ä–∞–µ–º " –ø–æ–≤—Ä–µ–∂–¥—ë–Ω")
        bad_arm.append(item[:-10])
    else:
        # –ò–Ω–∞—á–µ ‚Äî –ø—Ä–µ–¥–º–µ—Ç —Ü–µ–ª—ã–π, –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ —Å–ø–∏—Å–æ–∫ —Ü–µ–ª–æ–≥–æ —Å–Ω–∞—Ä—è–∂–µ–Ω–∏—è –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π
        good_arm.append(item)

# –í—ã–≤–æ–¥–∏–º —Å–ø–∏—Å–æ–∫ —Ü–µ–ª–æ–≥–æ —Å–Ω–∞—Ä—è–∂–µ–Ω–∏—è –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ (—Å –ø–æ–º–æ—â—å—é —Å—Ä–µ–∑–∞ [::-1])
print(f'–¶–µ–ª–æ–µ —Å–Ω–∞—Ä—è–∂–µ–Ω–∏–µ: {good_arm[::-1]}')

# –í—ã–≤–æ–¥–∏–º —Å–ø–∏—Å–æ–∫ –ø–æ–≤—Ä–µ–∂–¥—ë–Ω–Ω–æ–≥–æ —Å–Ω–∞—Ä—è–∂–µ–Ω–∏—è –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
