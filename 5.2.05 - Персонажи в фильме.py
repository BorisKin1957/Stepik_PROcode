'''Считай:

    целое число N --> количество персонажей;

    затем N строк вида: Имя, Роль (после запятой может быть пробел).

Для каждой строки сформируй кортеж ('Имя', 'Роль').

Выведи:

    если есть хотя бы один персонаж с ролью Главная роль --> сначала строку
    Персонажи, играющие главную роль:,
    затем по одному на строку каждый найденный кортеж (в стандартном виде Python, с одинарными кавычками);

    если таких персонажей нет --> выведи Персонажи отсутствуют.
'''

# Создаём пустой кортеж, который будет хранить всех персонажей с ролью "Главная роль"
result = ()

# Считываем количество персонажей
for _ in range(int(input())):
    # Считываем строку вида "Имя, Роль", разбиваем её по запятой и пробелу, создаём кортеж из двух элементов
    name = tuple(input().split(', '))

    # Проверяем, содержится ли подстрока "Главная роль" в кортеже
    if 'Главная роль' == name[1]:
    # Добавляем весь кортеж (имя, роль) в result
    # Запятая после name означает создание одноэлементного кортежа, а += объединяет кортежи
        result += name,

# Если в result есть хотя бы один элемент (то есть были найдены персонажи с главной ролью)
if result:
    print('Персонажи, играющие главную роль:')
    # Распаковываем кортеж result и выводим каждый элемент (каждый кортеж) на отдельной строке
    print(*result, sep='\n')
else:
    # Если ни одного персонажа с главной ролью не найдено
    print('Персонажи отсутствуют')