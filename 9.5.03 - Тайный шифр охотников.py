'''Шаг 1. Считайте две строки:
первая --> последовательность символов первого свитка,
вторая --> последовательность символов второго свитка.
Символы в каждой строке разделены пробелами. Пустая строка означает пустую последовательность.

Шаг 2. Определите отношения между свитками и выведите ровно одну из строк:

    Первый свиток скрыт в тексте второго. --> если все символы первого встречаются во
    втором в том же порядке (между ними могут быть другие символы).

    Второй свиток скрыт в тексте первого. --> если все символы второго встречаются в
    первом в том же порядке.

    Свитки имеют общие символы, но их порядок различен. --> если ни один не является
    подпоследовательностью другого, но есть хотя бы один общий символ.

    Свитки принадлежат разным эпохам. --> если общих символов нет вовсе.

Порядок проверки: сначала проверяйте, скрыт ли первый во втором; затем --> второй в первом;
затем наличие общих символов; затем --> отсутствие общих символов.

🧪 Пример 1 ввод

☠ ⚔ 🕯
☠ 🔮 ⚔ 📜 🕯


🧪 Пример 1 вывод

Первый свиток скрыт в тексте второго.'''

# Считываем строки и разбиваем их на списки символов по пробелам
vol_1 = input().split()  # Список символов первого свитка
vol_2 = input().split()  # Список символов второго свитка

# Преобразуем списки в множества для проверки наличия общих элементов
set_1, set_2 = set(vol_1), set(vol_2)
flag = False  # Флаг для отметки случая "есть общие символы, но не подпоследовательность"

# Проверяем: является ли первый свиток подпоследовательностью второго?
if set_1.issubset(set_2):
    # Получаем индексы символов первого свитка в порядке их появления во втором
    dict_sum = [vol_2.index(sym) for sym in vol_1]
    # Если индексы идут по возрастанию — это подпоследовательность
    if dict_sum == sorted(dict_sum):
        print('Первый свиток скрыт в тексте второго.')
    else:
        flag = True  # Есть символы, но порядок нарушен
# Проверяем обратное: второй свиток — подпоследовательность первого?
elif set_2.issubset(set_1):
    # Получаем индексы символов второго свитка в первом
    dict_sum = [vol_1.index(sym) for sym in vol_2]
    # Проверяем порядок
    if dict_sum == sorted(dict_sum):
        print('Второй свиток скрыт в тексте первого.')
    else:
        flag = True  # Есть символы, но порядок нарушен
else:
    # Нет общих символов вообще
    print('Свитки принадлежат разным эпохам.')

# Если ни один из случаев с подпоследовательностью не сработал, но есть общие символы
if flag:
    print('Свитки имеют общие символы, но их порядок различен.')

