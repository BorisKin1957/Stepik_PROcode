'''Получить словарь файлов (ключ=имя файла, значение=расширение).
2️⃣ Определить подозрительные файлы:

    Файлы с опасными расширениями (.exe, .dll, .bat, .scr).
    Файлы, маскирующиеся под текстовые (.txt.exe, .doc.dll).
    Файлы с точкой в конце имени (hacker.dll.).
    3️⃣ Вывести список поддельных файлов.
    4️⃣ Если нет подозрительных файлов, вывести "Поддельные файлы не найдены.".
'''

import json  # Подключаем модуль JSON для обработки входных данных в формате JSON

data = json.loads(input())  # Получаем словарь файлов из JSON-ввода

fake_ext = ['.exe', '.dll', '.bat', '.scr', '.']  # Опасные расширения, включая '.' для обработки файлов с точкой в конце
fake_files = []  # Список для хранения подозрительных файлов

# Перебираем все файлы: имя файла и его расширение
for file, ext in data.items():
    # Проверяем, является ли расширение опасным
    if ext in fake_ext:
        fake_files.append(file)

# Если найдены подозрительные файлы — выводим их
if fake_files:
    print(f'Поддельные файлы: {", ".join(fake_files)}')  # Исправлена ошибка: кавычки внутри f-строки
else:
    print('Поддельные файлы не найдены.')



