'''Шаг 1. Считайте одну строку без пробелов — это число (только символы цифр).
Шаг 2. Определите множество уникальных цифр кода (как целых чисел 0–9).
Шаг 3. Постройте множество возможных сумм двух разных цифр (каждую пару считать
один раз, порядок не важен).
Шаг 4. Найдите множество квадратов тех цифр, которые строго больше 5.
Шаг 5. Выведите три строки строго в таком формате (элементы внутри фигурных скобок
по возрастанию, разделитель: , ):

Уникальные цифры кода: {…}
Возможные суммы двух цифр: {…}
Квадраты цифр > 5: {…}

Если соответствующее множество пусто, выводите {}.'''

# Преобразуем введённую строку в множество уникальных цифр (целые числа)
digits = {int(sym) for sym in input() if sym.isdigit()}

# Выводим отсортированные уникальные цифры кода в требуемом формате
print(f'Уникальные цифры кода: {{{', '.join(map(str, sorted(digits)))}}}')

# Создаём пустое множество для хранения сумм двух разных цифр
sum_digits = set()

# Преобразуем множество цифр в отсортированный список для последующего перебора пар
digits = sorted(digits)

# Перебираем все пары индексов (i, j), где i < j, чтобы учесть каждую пару только один раз
for i in range(len(digits)):
    for j in range(i + 1, len(digits)):
        # Добавляем сумму двух разных цифр в множество
        sum_digits.add(digits[i] + digits[j])

# Если множество сумм пусто (например, была только одна цифра), заменяем его на строковое представление пустого множества
if not sum_digits:
    sum_digits = '{}'

# Выводим возможные суммы двух цифр в требуемом формате
print(f'Возможные суммы двух цифр: {sum_digits}')

# Создаём множество квадратов цифр, которые строго больше 5
sq_digits = {num ** 2 for num in set(digits) if num > 5}

# Выводим квадраты цифр > 5 в требуемом формате
print(f'Квадраты цифр > 5: {{{', '.join(map(str, sorted(sq_digits)))}}}')



