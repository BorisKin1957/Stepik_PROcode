    # –ü–∞—Ä–∞–º–µ—Ç—Ä 1 –æ–∑–Ω–∞—á–∞–µ—Ç: –∑–∞–º–µ–Ω–∏—Ç—å —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤–æ–µ –≤—Ö–æ–∂–¥–µ–Ω–∏–µ
    encrypted_message = encrypted_message.replace(result, '', 1)