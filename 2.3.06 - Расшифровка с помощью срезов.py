    # –ò–∑–≤–ª–µ–∫–∞–µ–º –ø–æ–¥—Å—Ç—Ä–æ–∫—É (—Å—Ä–µ–∑) –∏–∑ —Å–æ–æ–±—â–µ–Ω–∏—è –ø–æ –∑–∞–¥–∞–Ω–Ω—ã–º –∏–Ω–¥–µ–∫—Å–∞–º [start:end)
