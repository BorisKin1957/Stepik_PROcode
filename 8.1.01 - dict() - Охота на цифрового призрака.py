'''
    Получить словарь с расшифровкой (ключ=код, значение=слово).
    Получить зашифрованную строку (слова-коды через пробел).
    Заменить коды на реальные слова.
    Ограничение: не используйте sorted
'''

import json  # Подключаем модуль JSON для обработки входных данных в формате JSON

data = json.loads(input())  # Считываем строку в формате JSON и преобразуем её в словарь Python
codes = input().split()  # Считываем строку с кодами и разбиваем её на список по пробелам

# Формируем список слов: для каждого кода ищем соответствующее слово в словаре,
# если код не найден — возвращаем '<Unknown>'
result = [data.get(code, '<Unknown>') for code in codes]

# Выводим расшифрованные слова через пробел
print(*result)