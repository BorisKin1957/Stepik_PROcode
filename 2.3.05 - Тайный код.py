'''Считай одну строку --> код.
Сначала подсчитай, сколько раз в строке встречается слово магия без учёта
регистра (считай именно отдельное слово).

Далее, в зависимости от количества найденных слов:

если количество чётное (включая 0) --> удали все пробелы из исходной строки
 переверни получившуюся строку;

если количество нечётное --> из исходной строки извлеки все символы,
которые встречаются ровно один раз (учитывай регистр), сохранив их исходный порядок.

Выведи две строки:

Магия найдена <число> раз(а).

Результат: <полученная_строка>'''

text = input()

count = text.lower().count('магия')
result = ''

if count % 2 == 0:
    result = text.replace(' ', '')[::-1]
else:
    for sym in text:
        if text.count(sym) == 1:
            result += sym

print(f'Магия найдена {count} раз(а).')
print(f'Результат: {result}')

'''Код использует text.lower().count('магия'), что не гарантирует, что слово 
"магия" стоит отдельно. Например, в слове "самагия" или "магияльно" тоже будет найдено "магия".
Если по условию нужно искать именно отдельное слово, следует использовать 
более точный способ — например, разбиение на слова:'''