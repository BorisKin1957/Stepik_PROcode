'''Считайте одну строку с данными о камнях. Данные заданы парами и разделены запятыми,
каждая пара имеет вид:
<тип> <сила>
Например:
огненный 5, водяной -3, огненный -2, земной 4, водяной 1

Требуется:

    Разбить ввод на пары и для каждой пары сформировать кортеж (тип, сила).

    Собрать кортежи камней в общий кортеж.

    Для каждого типа отдельно вычислить сумму сил (положительные значения прибавляются,
    отрицательные --> вычитаются).

    Вывести строки для каждого встреченного типа в порядке первого появления в исходных
    данных в формате:
    Общая сила <тип> камень: <сумма>

⚠️ Ограничения: не используйте set - dict-sorted'''

# Создаём пустой список для хранения пар (тип, сила)
data = []

# Читаем строку от пользователя, разделяем её по запятой и пробелу ', ' — получаем список строк вида "огненный 5"
for item in input().split(', '):
    # Каждую подстроку вроде "огненный 5" разбиваем по пробелу: получаем [тип, сила]
    kind, force = item.split()
    # Преобразуем силу в целое число и добавляем кортеж (тип, сила) в список data
    data.append((kind, int(force)))

# Теперь будем обрабатывать список data, пока в нём больше одного элемента
while len(data) > 1:
    # Берём первый элемент: его тип и силу
    kind = data[0][0]  # тип первого камня
    force = data[0][1]  # сила первого камня (уже число)

    # Проверяем, встречается ли такой же тип в оставшихся элементах (начиная с индекса 1)
    # sum(data[1:], ()) создаёт плоский кортеж из всех элементов начиная со второго — например, ('водяной', -3, 'огненный', -2, ...)
    # Это трюк, чтобы проверить, есть ли `kind` среди типов в оставшихся данных
    if kind in sum(data[1:], ()):
        # Проходим по оставшимся парам (начиная с индекса 1)
        for k, v in data[1:]:
            # Если тип совпадает с текущим `kind`
            if k == kind:
                # Добавляем силу этого камня к общей силе
                force += v
                # Удаляем найденную пару из списка (это может вызвать проблемы при итерации!)
                data.remove((k, v))

    # Удаляем первый элемент (мы его уже обработали)
    data.remove(data[0])
    # Выводим общую силу для этого типа камня
    print(f'Общая сила {kind} камень: {force}')

# После цикла остаётся один элемент — выводим его отдельно
print(f'Общая сила {data[0][0]} камень: {data[0][1]}')