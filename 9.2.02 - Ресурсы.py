'''Шаг 1. Считайте одну строку, содержащую названия ресурсов, разделённые символом ;.
Пробелы вокруг названий следует игнорировать.
Шаг 2. Определите множество уникальных ресурсов (без повторов).

Шаг 3. По множеству уникальных ресурсов найдите:

— общее количество уникальных ресурсов;
— самый ценный ресурс: ресурс с максимальной длиной названия; если таких несколько,
выберите тот, который идёт первым в алфавитном порядке;
— самый редкий ресурс: ресурс с минимальной длиной названия; если таких несколько,
выберите тот, который идёт первым в алфавитном порядке;
— суммарную «ценность»: сумму длин всех уникальных названий.

Шаг 4. Выведите результаты строго в формате строк (каждую с новой строки):'''

# Шаг 1: Разделение по ';' и удаление пробелов
lst = [x.strip() for x in input().strip().split(';')]

# Шаг 2: Уникальные ресурсы, отсортированные по алфавиту
resources = sorted(set(lst))

# Шаг 3: Расчёт характеристик
print(f'Общее количество уникальных ресурсов: {len(resources)}')
print(f'Самый ценный ресурс: {max(resources, key=len)}')
print(f'Самый редкий ресурс: {min(resources, key=len)}')
print(f'Суммарная ценность ресурсов: {sum(map(len, resources))}')

# Сортировка только по длине — алфавитный порядок для равных длин сохраняется
sorted_by_len = sorted(resources, key=len)
print(f'Ресурсы в отсортированном порядке: {", ".join(sorted_by_len)}')