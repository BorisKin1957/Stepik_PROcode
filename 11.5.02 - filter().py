'''Шаг 1. Считайте одну строку с названиями животных, разделёнными запятыми
(пробелы вокруг названий игнорируйте).
Шаг 2. Оставьте только те названия, которые начинаются с заглавной латинской буквы C.
Шаг 3. Из них выберите только те, у которых длина строго больше 3 символов.
Шаг 4. Отсортируйте получившиеся названия по длине в порядке возрастания.
Шаг 5. Выведите итоговый список (Python-список) как в примере.

Требование: используйте lambda вместе с filter() и sorted().'''

# Шаг 1: Считываем строку, разделяем её по запятым и убираем лишние пробелы вокруг каждого названия
animals = [animal.strip() for animal in input().split(',')]

# Шаг 2 и 3: Фильтруем названия — оставляем только те, что начинаются на 'C' или 'c' И имеют длину > 3
# Используем lambda-функцию внутри filter(): проверяем первую букву (в нижнем регистре == 'c') и длину
# Затем сортируем отфильтрованные элементы по длине (в порядке возрастания) с помощью key=len
result = sorted(
    filter(
        lambda animal: animal[0].lower() == 'c' and len(animal) > 3,
        animals
    ),
    key=len
)

# Шаг 5: Выводим результат в виде Python-списка (например: ['Cat', 'Crab', 'Cheetah'])
print(result)