'''На вход подаётся одна строка, содержащая слова, разделённые пробелами.
Требуется найти слово с наибольшей частотой появления. Если таких слов несколько,
вывести то, что больше в лексикографическом порядке. Выведите искомое слово единожды.'''


# Создаём пустой словарь для хранения частоты каждого слова
words_dict = {}

# Читаем строку с входа, разбиваем её на слова по пробелам
for word in input().split():
    # Увеличиваем счётчик слова в словаре; если слово встречается впервые — начинаем с 0
    words_dict[word] = words_dict.get(word, 0) + 1

# Сортируем элементы словаря (пары "слово: частота") по двум критериям:
# 1. По возрастанию частоты (x[1])
# 2. При равной частоте — по лексикографическому порядку (x[0])
# Таким образом, искомое слово окажется последним в списке
result = sorted(words_dict.items(), key=lambda x: (x[1], x[0]))

# Выводим первое слово из последней пары (то есть искомое слово)
print(result[-1][0])


'''
Пояснение логики:

sorted(..., key=lambda x: (x[1], x[0])) сортирует сначала по частоте 
(второй элемент кортежа), затем по слову (лексикографически).
Так как сортировка по умолчанию — по возрастанию, то самое частое слово 
(и при равенстве — лексикографически большее) окажется в конце.
Поэтому берём result[-1][0] — последний элемент списка (наиболее частый и 
лексикографически наибольший при равенстве), 
и из него — только слово.
✅ Код работает корректно для поставленной задачи.
'''