''''1️⃣ Получить словарь дешифровки (ключ=зашифрованный хеш, значение=пароль).
2️⃣ Получить список зашифрованных паролей.
3️⃣ Попытаться расшифровать каждый пароль:

    Очистить от лишних символов (*, #, @, !, $).
    Найти в словаре и вывести.
    Если не найден, вывести "Не удалось расшифровать пароль".
'''

from json import loads
from re import findall

# Считываем JSON-словарь: ключи — хеши, значения — пароли
data = loads(input())

# Считываем строку с зашифрованными паролями и извлекаем только буквенно-цифровые части (удаляем *, #, @, и т.д.)
codes = findall(r'\w+', input())

# Для каждого извлечённого кода пытаемся найти соответствующий пароль в словаре
# Если код не найден — подставляем сообщение об ошибке
result = [data.get(code, 'Не удалось расшифровать пароль') for code in codes]

# Выводим результаты через пробел
print(*result)
