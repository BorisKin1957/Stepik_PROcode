'''Шаг 1. Считайте одну строку --> текстовое сообщение.

Шаг 2. Постройте «карту следов» --> словарь, где ключом является символ строки, 
а значением список индексов всех его появлений.

Шаг 3. Учитывайте пробелы и знаки препинания как обычные символы. Индексация начинается с 0.

Шаг 4. Выведите полученный словарь.'''


trail_map = {}  # Инициализация пустого словаря для хранения символов и их индексов

# Перебираем каждый символ во входной строке с его индексом
for index, symbol in enumerate(input()):
    # Если символ уже есть в словаре, добавляем индекс к списку
    # Если нет — создаём новый список с этим индексом
    trail_map.setdefault(symbol, []).append(index)

# Выводим итоговый словарь — «карту следов»
print(trail_map)