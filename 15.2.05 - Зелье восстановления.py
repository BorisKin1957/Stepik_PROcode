'''
Пользователь вводит названия зелий в виде строки через запятую.

Нужно:
    Сформировать список зелий.
    Определить и вывести отсортированный список уникальных зелий.
    Вывести общее количество зелий.
    Вывести количество уникальных зелий.
    Вычислить и вывести самое часто встречающееся зелье и количество его повторов.

Если несколько зелий одинаково распространены, вывести то, что стоит первым в отсортированном списке.
'''

# Импортируем класс Counter из модуля collections — он позволяет легко подсчитывать количество элементов в списке
from collections import Counter

# Считываем строку от пользователя, разбиваем её по запятым и убираем пробелы вокруг каждого названия зелья
potions = [potion.strip() for potion in input().split(',')]

# Преобразуем список зелий во множество (убираем дубликаты), затем сортируем в алфавитном порядке
unique = sorted(set(potions))

# Используем Counter, чтобы посчитать, сколько раз встречается каждое зелье
# Затем находим самое частое: сортируем сначала по убыванию частоты (-x[1]), затем по имени зелья (x[0])
# min выбирает первое подходящее значение в таком порядке — это будет самое частое и при равенстве — лексикографически первое
common = min(Counter(potions).items(), key=lambda x: (-x[1], x[0]))

# Выводим отсортированный список уникальных зелий
print(f'Уникальные зелья: {unique}')

# Выводим общее количество зелий (включая дубликаты)
print(f'Всего зелий: {len(potions)}')

# Выводим количество уникальных зелий
print(f'Количество уникальных зелий: {len(unique)}')

# Выводим название самого частого зелья и сколько раз оно встречается
print(f'Самое частое зелье: {common[0]} ({common[1]} раз(а))')