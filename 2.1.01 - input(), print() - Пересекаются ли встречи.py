'''Задание
Даны времена начала и конца двух встреч в минутах от начала дня.
Считайте четыре целых числа (по одному в строке):
start1, end1, start2, end2.

Считаем, что встречи пересекаются, если у них есть общий промежуток положительной
длины (простое касание концами, как end1 == start2, --> не пересечение).

Если встречи пересекаются, выведите на отдельных строках:

Да

вторую встречу, преобразованную в формат ЧЧ:ММ - ЧЧ:ММ (с ведущими нулями).

Если не пересекаются --> выведите только Нет.

Гарантируется, что значения не превышают 24 часа (0–1440).

⚠️ Ограничения
Нельзя использовать готовые инструменты для сортировки и коллекций:
sorted, list, set, dict, range.'''

# Считываем времена начала и конца двух встреч
start1, end1, start2, end2 = int(input()), int(input()), int(input()), int(input())

# Проверяем, пересекаются ли встречи.
# Встречи пересекаются, если есть общий интервал положительной длины.
# Условие: начало первой < конца второй И начало второй < конца первой.
# Касание (например, end1 == start2) НЕ считается пересечением.
if start1 < end2 and start2 < end1:
    # Форматируем время второй встречи: переводим минуты в ЧЧ:ММ с ведущими нулями
    start2_hours = start2 // 60
    start2_minutes = start2 % 60
    end2_hours = end2 // 60
    end2_minutes = end2 % 60
    # Выводим "Да" и временной диапазон второй встречи
    print(f'Да\n{start2_hours:02d}:{start2_minutes:02d} - {end2_hours:02d}:{end2_minutes:02d}')
else:
    # Встречи не пересекаются — выводим "Нет"
    print('Нет')
