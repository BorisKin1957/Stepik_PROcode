'''На вход подаётся список pets, где каждый элемент кортеж вида (кличка собаки,
имя мага, фамилия мага, возраст мага). Требуется создать словарь,
 котором ключ --> кортеж (имя, фамилия, возраст) владельца, а значение -->
 список кличек его собак в порядке появления в исходных данных.
  получившийся словарь.

Кортежи неизменяемы и могут использоваться как ключи словаря.
Ввод осуществляется строкой: pets = eval(input()).'''

# pets = [("Барсик", "Иван", "Иванов", 35),
#         ("Рэкс", "Пётр", "Петров", 42),
#         ("Бобик", "Иван", "Иванов", 35),
#         ("Шарик", "Мария", "Смирнова", 28),
#         ("Тузик", "Пётр", "Петров", 42),
#         ("Рокси", "Елена", "Кузнецова", 31),
#         ("Майло", "Иван", "Иванов", 35),
#         ("Лайма", "Мария", "Смирнова", 28)
#         ]

# Считываем входные данные — список кортежей в формате (кличка, имя, фамилия, возраст)
pets = eval(input())

# Создаём пустой словарь, в котором ключом будет кортеж (имя, фамилия, возраст) мага,
# а значением — список кличек его собак
dogs_owners = {}

# Перебираем каждый элемент списка pets (каждый питомец)
for pet in pets:
    # pet[1:] — это срез кортежа: (имя, фамилия, возраст) мага (исключаем кличку — pet[0])
    # pet[0] — это кличка собаки
    # Используем .get(pet[1:], []) чтобы получить текущий список кличек этого владельца
    # Если такого владельца ещё нет в словаре — вернётся пустой список []
    # Добавляем кличку pet[0] в конец этого списка
    dogs_owners[pet[1:]] = dogs_owners.get(pet[1:], []) + [pet[0]]

# Выводим итоговый словарь
print(dogs_owners)