# Начало бесконечного цикла, который будет запрашивать ввод до тех пор,
# пока не будет получено корректное (ненулевое) целое число.
while True:
    try:
        # Попытка считать ввод пользователя и преобразовать его в целое число.
        res = int(input())

        # Проверка, действительно ли res является целым числом (избыточна, так как int() уже гарантирует это).
        # Если преобразование прошло успешно, проверяем, равно ли число нулю.
        if isinstance(res, int):
            if res == 0:
                # Явно вызываем исключение ZeroDivisionError, если введён ноль.
                raise ZeroDivisionError
            else:
                # Если число не ноль — выводим сообщение об успешной проверке и удвоенное значение.
                print(f'Доступ разрешён. Удвоенный код: {2 * res}')
                break

    except ValueError:
        print('Ошибка: это не число. Попробуйте снова.')
    except ZeroDivisionError:
        print('Ошибка: код не может быть равен нулю. Попробуйте снова.')
