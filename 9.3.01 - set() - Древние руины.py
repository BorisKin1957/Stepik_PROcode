'''Шаг 1. Считайте натуральное число N --> количество мест раскопок.
Шаг 2. Считайте следующие N строк. Каждая строка содержит номера артефактов, разделённые пробелами.
Шаг 3. Определите общее число различных (уникальных) номеров артефактов, встречающихся хотя бы в одном месте.
Шаг 4. Выведите единственное число --> количество уникальных артефактов.'''

# Создаём пустое множество для хранения уникальных номеров артефактов
result = set()

# Так проще (альтернативное решение с использованием операции объединения множеств):
# for _ in range(int(input())):
#     result |= set(input().split())  # Объединяем текущее множество с новыми артефактами

# Такого решения ждут (явный перебор и добавление каждого номера):
for _ in range(int(input())):  # Повторяем N раз, где N — количество мест раскопок
    for number in set(input().split()):  # Разбиваем строку на номера и преобразуем в множество (убираем дубли в одной строке)
        result.add(number)  # Добавляем каждый уникальный номер артефакта в общее множество

print(len(result))  # Выводим количество уникальных артефактов