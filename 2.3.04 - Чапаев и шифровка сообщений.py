text = input()[::-1].replace('_', ' ')

result = ''

for sym in text:
    if ord(sym) == 1040:
        result += '–Ø'
    elif ord(sym) == 1072:
        result += '—è'
    else:
        if sym.isalpha():
            result += chr(ord(sym) - 1)
        else:
            result += sym

