'''Шаг 1. Считайте целое n --> количество записей. Затем прочитайте n строк формата:
Имя Количество_лайков Количество_комментариев.

Шаг 2. Сгруппируйте записи по пользователям без учёта регистра имени.
Имя в отчёте должно быть выведено в том написании, в котором оно встретилось впервые.

Шаг 3. Для каждого уникального пользователя выведите в отдельной строке, в порядке первого появления:
<Имя_пользователя> <Общее_количество_лайков> <Общее_количество_комментариев>.
Значения разделяйте пробелом.'''


users = {}  # Словарь для хранения суммарных лайков и комментариев по каждому пользователю (в нижнем регистре)
first_appearance = {}  # Словарь для сохранения оригинального написания имени при первом появлении

# Читаем n записей от пользователей
for _ in range(int(input())):
    nickname, like_n, comm_n = input().split()  # Разделяем введённую строку на имя, количество лайков и комментариев
    # Проверяем, встречали ли мы уже этого пользователя (без учёта регистра)
    if nickname.lower() not in users:
        # Если пользователь встречается впервые, сохраняем его данные
        users[nickname.lower()] = [int(like_n), int(comm_n)]  # [лайки, комментарии]
        first_appearance[nickname.lower()] = nickname  # Сохраняем оригинальное написание имени
    else:
        # Если пользователь уже встречался, обновляем его данные
        users[nickname.lower()][0] += int(like_n)  # Добавляем лайки
        users[nickname.lower()][1] += int(comm_n)  # Добавляем комментарии

# Выводим результаты в порядке первого появления пользователей
for nickname, value in users.items():
    # Печатаем оригинальное имя, общее количество лайков и комментариев
    print(first_appearance[nickname], value[0], value[1])

