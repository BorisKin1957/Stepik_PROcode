''''Шаг 1. Объявите функцию transliterate(text), которая:

    приводит весь текст к нижнему регистру;

    заменяет русские буквы на латинские по словарю ниже;

    возвращает полученную строку.

Шаг 2. Объявите декоратор clean_text(chars), который:

    принимает строку символов chars (набор, который требуется заменить);

    возвращает декоратор, оборачивающий функцию;

    в обёртке:

        получает результат исходной функции (строка);

        заменяет каждый символ из chars на '+';

        схлопывает серии подряд идущих '+' до одного '+';

        возвращает очищенную строку.

Шаг 3. Примените декоратор к transliterate со строкой символов
"?!:;,. " (вопрос, восклицание, двоеточие, точка с запятой, запятая, точка и пробел):

@clean_text("?!:;,. ")
def transliterate(text): ...

Требования:

    Регистр: всегда нижний.

    Замена пунктуации и пробелов --> на '+'.

    Повторяющиеся '+' должны быть удалены (сведены к одному).

    Используйте словарь s для замены только русских букв; прочие символы (кроме chars) оставляйте как есть.

'''

# Импортируем модуль re для работы с регулярными выражениями
# Нужно для эффективного схлопывания нескольких '+' в один
import re

# Декоратор clean_text принимает параметр chars — строку символов, которые нужно заменить на '+'
def clean_text(chars):
    # Внутренняя функция decorator — это сам декоратор, который будет применяться к функции
    def decorator(func):
        # Обёртка wrapper заменяет поведение оригинальной функции
        def wrapper(text):
            # Вызываем оригинальную функцию (transliterate) и получаем её результат (строку)
            lst = list(func(text))  # Преобразуем строку в список символов для удобства посимвольной замены

            # Проходим по каждому индексу списка (чтобы можно было изменять элементы по месту)
            for i in range(len(lst)):
                # Если текущий символ содержится в наборе нежелательных символов (chars)
                if lst[i] in chars:
                    # Заменяем его на '+'
                    lst[i] = '+'

            # Собираем строку обратно из списка символов
            result = ''.join(lst)

            # Используем регулярное выражение для замены одной или более последовательных '+' на один '+'
            # Например: '+++' → '+', 'a++b' → 'a+b'
            result = re.sub(r'\++', '+', result)

            # Возвращаем очищенную строку
            return result
        # Возвращаем обёрнутую функцию (wrapper)
        return wrapper
    # Возвращаем декоратор (функцию, которая оборачивает другую функцию)
    return decorator

# Применяем декоратор @clean_text с аргументом "?!:;,. "
# Это означает: после выполнения transliterate замени все эти символы на '+' и схлопни повторы
@clean_text("?!:;,. ")
def transliterate(text: str) -> str:
    # Словарь транслитерации: сопоставляет строчные русские буквы их латинским эквивалентам
    translit_map = {
        'ё': 'yo', 'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l',
        'м': 'm', 'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
        'у': 'u', 'ф': 'f', 'х': 'h', 'ц': 'c', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya'
    }

    # Инициализируем пустую строку для построения результата
    result = ''

    # Перебираем каждый символ во входном тексте
    for char in text:
        # Приводим символ к нижнему регистру и проверяем, есть ли он в словаре транслитерации
        if char.lower() in translit_map:
            # Если да — добавляем соответствующее латинское написание
            result += translit_map[char.lower()]
        else:
            # Если нет (например, цифра, латинская буква или знак) — просто добавляем символ в нижнем регистре
            result += char.lower()

    # Возвращаем построенную строку с транслитерацией
    return result

# Считываем строку с клавиатуры, передаём её в transliterate,
# которая автоматически проходит через декоратор clean_text,
# затем печатаем итоговую очищенную и транслитерированную строку
print(transliterate(input()))
