'''Шаг 1. Объявите рекурсивную функцию flatten_data(data) -> list, которая принимает
список произвольной вложенности.
Шаг 2. Функция должна вернуть новый список со всеми значениями исходного списка,
извлечёнными в порядке обхода слева направо.
Шаг 3. Вложенными контейнерами считаются только списки (list).
Строки и другие типы считаются атомарными значениями и не итерируются.

Подсказки по требованиям:
— Рекурсия обязательна.
— Пустые вложенные списки не добавляют элементов в результат.
— Порядок элементов должен сохраняться.'''


def flatten_data(data: list, result=None) -> list:
    """
    Рекурсивно "расплющивает" список произвольной вложенности.
    Учитываются только вложенные списки. Другие типы (строки и т.д.) — атомарны.
    """
    if result is None:
        result = []

    for item in data:
        if isinstance(item, list):
            flatten_data(item, result)  # рекурсивно обрабатываем вложенный список
        else:
            result.append(item)  # добавляем атомарное значение

    return result



dataset = [42, [3.14, [True, False], "GPT-4"], ["Python", [10, 20], ["AI", [100, 200, [300]]]], "Data Science"]
print(flatten_data(dataset))  # вызов выполняет скрытая часть тестов