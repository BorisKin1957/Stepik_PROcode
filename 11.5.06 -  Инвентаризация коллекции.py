'''Шаг 1. Считайте целое число N --> количество записей.
Шаг 2. Считайте N строк в формате: Название, год, стоимость.
Название может содержать пробелы; год и стоимость --> числовые значения; разделитель -->
запятая с пробелом (как в примере).

Шаг 3. С помощью filter() и lambda исключите артефакты, у которых год обнаружения меньше 2000.
Шаг 4. С помощью map() и lambda увеличьте стоимость оставшихся артефактов на 25%
(умножьте на 1.25) и округлите новую стоимость до 2 знаков после запятой.
Шаг 5. Отсортируйте результат с помощью sorted() и lambda по новой стоимости по возрастанию.
Шаг 6. Выведите итоговый список как Python-список кортежей в формате:
[(Название, год, новая_стоимость), ...]

Требования: использовать lambda, filter(), map(), sorted() для соответствующих шагов.'''

data = [input().split(', ') for _ in range(int(input()))]

# Формируем список кортежей: (Название, год, стоимость * 1.25), но ещё без фильтрации по году
artifacts = [(item[0].strip(), int(item[1]), round(float(item[2]) * 1.25, 2)) for item in data]

# Фильтруем: оставляем только те артефакты, у которых год >= 2000
after_2000 = filter(lambda item: item[1] >= 2000, artifacts)

# Сортируем отфильтрованные артефакты по новой стоимости (по возрастанию)
result = sorted(after_2000, key=lambda item: item[2])

# Выводим результат в требуемом формате
print(f'Артефакты после обработки:\n{result}')
