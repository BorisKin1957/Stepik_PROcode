'''
🎯 Задание:

Пользователь вводит количество врагов.
Затем для каждого врага он вводит три строки:

    Имя врага (строка)
    Урон врага (целое число)
    Броня врага (целое число)

Тебе нужно:

    Создать словарь с информацией о каждом враге: имя -> [урон, броня].
    Найти врага с максимальной угрозой (угроза = урон + броня).
    Вычислить итоговый урон по врагу (наш урон = 60, минус его броня, но не меньше 0).
    Вывести:
        Имя самого опасного врага.
        Уровень угрозы (урон + броня).
        Итоговый урон.
        Исход боя: 'Победа' или 'Поражение'.
'''

foes = {}  # Словарь: имя -> [урон, броня]

# Ввод данных о врагах
for _ in range(int(input())):
    name = input()  # Имя врага
    damage = int(input())  # Урон врага
    armor = int(input())  # Броня врага
    foes[name] = [damage, armor]

# Нахождение врага с максимальной угрозой (сумма урона и брони)
enemy = max(foes.items(), key=lambda x: sum(x[1]))[0]
print(f'Самый опасный враг: {enemy}')

# Уровень угрозы — это сумма урона и брони
danger = sum(foes[enemy])
print(f'Уровень угрозы: {danger}')

# Итоговый урон: наш урон = 60, вычитаем броню врага, но не меньше 0
loss = max(0, 60 - foes[enemy][1])
print(f'Итоговый урон по врагу: {loss}')

# Определяем исход боя
result = 'Победа' if loss > 0 else 'Поражение'
print(f'Исход боя: {result}')


