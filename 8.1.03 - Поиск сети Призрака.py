'''
    Получить словарь ID → (ник, платформа).
    Получить ID хакера.
    Найти все платформы и никнеймы, где он зарегистрирован.
    Вывести список платформ и соответствующих им никнеймов.
    Если ID не найден, вывести "ID не найден.".
'''

import json  # Подключаем модуль JSON для обработки входных данных в формате JSON

data = json.loads(input())  # Считываем и парсим входной JSON-объект — словарь с данными пользователей

id = input()  # Считываем ID хакера, которого нужно найти

# Ищем записи по ключу id; если найдено, result будет списком из одного элемента — списка пар (платформа, ник)
result = [data[key] for key in data.keys() if key == id]

if result:
    # Подсчитываем общее количество платформ: суммируем списки внутри result и получаем длину
    amount = len(sum(result, []))
    print(f'Призрак найден на {amount} платформ(ах):')
    # Перебираем каждую платформу в найденных данных
    for platform in result:
        # Для каждой пары (сайт, ник) выводим её в заданном формате
        for nickname, website in platform:
            print(f'- {website}: {nickname}')
else:
    # Если пользователь с таким ID не найден
    print('ID не найден.')