'''Твой код должен:
Принять два набора символов (по отдельности, через пробел).
Создать два кортежа.
Объединить их в один кортеж.
Удалить повторяющиеся символы.
Если количество символов чётное → отсортировать.
Если количество символов нечётное → перевернуть порядок.
Вывести итоговый кортеж.'''

# Запрашиваем у пользователя два набора символов
# split() разбивает ввод по пробелам, получаем список символов
# Складываем два списка: первый ввод и второй ввод
symbols = input().split() + input().split()

# Создаём пустой список для хранения уникальных символов (без повторений)
result = []

# Перебираем каждый символ из объединённого списка
for sym in symbols:
    # Если символ ещё не добавлен в result — добавляем его
    if sym not in result:
        result.append(sym)

# Проверяем, чётная ли длина итогового списка
if len(result) % 2:  # True, если длина нечётная
    # Переворачиваем список и преобразуем в кортеж
    result = tuple(result[::-1])
else:  # Если длина чётная
    # Сортируем список по алфавиту/порядку и преобразуем в кортеж
    result = tuple(sorted(result))

# Выводим итоговый кортеж
print(result)