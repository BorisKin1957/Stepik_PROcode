'''Твой код должен:
Принять строку из символов (разделённых пробелами).
Создать кортеж из этих символов.
Удалить лишние символы, но сохранить порядок букв. (оставить только буквы!)
Объединить буквы в строку.
Определить ключевую букву (центральную в послании).
Вывести полученное послание и ключевую букву.
'''

string = tuple([sym for sym in input() if sym.isalpha()])

# Объединяем все собранные буквы из списка в одну строку
result = ''.join(string)

# Вычисляем индекс центрального символа строки (целочисленное деление)
# Для чётной длины — это будет правый из двух центральных
index = len(result) // 2

# Выводим очищенное сообщение (только буквы)
print(result)

# Выводим ключевую (центральную) букву сообщения
print(f'Ключевая буква: {result[index]}')