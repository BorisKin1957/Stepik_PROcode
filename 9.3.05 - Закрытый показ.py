'''Шаг 1. Считайте целое число n --> сколько номеров уже внесено.

Шаг 2. Считайте одну строку с n целыми числами --> уже внесённые номера (повторы возможны).

Шаг 3. Считайте целое число m --> сколько номеров приходит дальше.

Шаг 4. Затем считайте m строк, в каждой строке одно целое число x --> очередной номер.

Шаг 5. Создайте множество guests из уже внесённых номеров.

Шаг 6. Создайте переменную adder и запишите в неё метод guests.add.

Шаг 7. Для каждого нового номера добавляйте его через adder(x) и считайте, сколько раз
множество действительно увеличилось. Это число сохраните в added_count.

Шаг 8. Выведите:
первая строка --> added_count
вторая строка --> все элементы множества guests в отсортированном виде через пробел

Шаг 9. Контракт: после выполнения должны существовать guests, adder, added_count.'''


# Шаг 1: Считываем количество уже внесённых номеров (n)
# На самом деле, это значение не используется напрямую, так как следующая строка читает все номера сразу
input()

# Шаг 2: Считываем строку с n целыми числами и преобразуем их в множество
# Используем map(int, ...) для перевода строк в числа, set() — чтобы убрать дубликаты и хранить уникальные номера
guests = set(map(int, input().split()))

# Шаг 6: Сохраняем метод .add множества guests в переменную adder
# Теперь adder(x) будет эквивалентно guests.add(x)
adder = guests.add

# Шаг 7: Счётчик, сколько новых уникальных гостей было добавлено
added_count = 0

# Шаг 3: Считываем количество новых номеров (m)
# Шаг 4: Читаем m строк по одному номеру
for _ in range(int(input())):
    # Запоминаем размер множества до добавления
    before = len(guests)
    # Добавляем новый номер через функцию adder
    adder(int(input()))
    # Проверяем, увеличился ли размер множества (значит, элемент был новым)
    after = len(guests)
    if after > before:
        added_count += 1  # Увеличиваем счётчик, если гость действительно новый

# Шаг 8: Выводим результат
# Первая строка — количество реально добавленных уникальных номеров
print(added_count)
# Вторая строка — все номера из множества в отсортированном порядке, разделённые пробелом
print(*sorted(guests))

# Контракт выполнен: переменные guests (множество), adder (метод add), added_count (целое число) существуют
