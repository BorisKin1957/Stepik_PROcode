'''Шаг 1. Объяви декоратор cache_one_arg(func), который принимает функцию и возвращает обёртку wrapper.
Шаг 2. Декоратор предназначен для функций, принимающих один позиционный аргумент x.
Шаг 3. Внутри декоратора создай словарь кэша (в замыкании): ключ это x, значение это результат функции.
Шаг 4. Обёртка должна работать так:
Если x встречается впервые -> напечатай MISS <x>, затем вызови исходную функцию,
сохрани результат в кэш и верни его.
Если x уже есть в кэше -> напечатай HIT <x> и верни сохранённый результат, исходную функцию не вызывай.
Шаг 5. Объяви функцию telemetry(x), которая возвращает x * x + 1. Ничего не печатает.
Шаг 6. Примени декоратор к функции с помощью @cache_one_arg.'''

# Декоратор cache_one_arg принимает функцию `func` как аргумент
def cache_one_arg(func):
    # Создаём словарь `cash` в замыкании декоратора.
    # Он будет хранить уже вычисленные результаты: ключ — аргумент `x`, значение — результат функции.
    cash = dict()

    # Обёртка (wrapper), которая будет заменять собой оригинальную функцию
    def wrapper(number):

        # Проверяем, есть ли аргумент `number` в кэше
        if number in cash:
            # Если есть — выводим сообщение HIT и возвращаем сохранённый результат
            print(f'HIT {number}')
            return cash[number]
        else:
            # Если нет — выводим сообщение MISS
            print(f'MISS {number}')
            # Вычисляем результат вызова оригинальной функции
            result = func(number)
            # Сохраняем результат в кэш
            cash[number] = result
            # Возвращаем результат
            return result

    # Возвращаем обёрнутую функцию
    return wrapper


# Применяем декоратор @cache_one_arg к функции telemetry
@cache_one_arg
def telemetry(x):
    # Функция возвращает x² + 1, ничего не печатает
    return x * x + 1


# Первый вызов telemetry(5) — значение 5 отсутствует в кэше → MISS 5, вычисляется 5*5+1 = 26
print(telemetry(5))  # Вывод: MISS 5 \n 26

# Второй вызов telemetry(5) — значение 5 уже в кэше → HIT 5, возвращается 26 без вычислений
print(telemetry(5))  # Вывод: HIT 5 \n 26

print(telemetry(0))
print(telemetry(0))