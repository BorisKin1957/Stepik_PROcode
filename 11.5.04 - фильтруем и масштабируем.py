'''Шаг 1. Считайте одну строку с целыми числами, разделёнными пробелами.
Шаг 2. С помощью filter() и lambda оставьте только числа, которые делятся на 3 без остатка.
Шаг 3. С помощью map() и lambda умножьте каждое отфильтрованное число на 2.
Шаг 4. Отсортируйте результат по возрастанию с помощью sorted().
Шаг 5. Выведите итог в виде Python-списка.

Требования: использовать lambda, filter(), map() и sorted() для соответствующих шагов.'''

# Преобразуем ввод пользователя в список целых чисел и фильтруем те, что делятся на 3
numbers = filter(lambda num: num % 3 == 0, map(int, input().split()))

# Умножаем каждое отфильтрованное число на 2 с помощью map() и сортируем список
result = sorted(map(lambda num: num * 2, numbers))

# Выводим отсортированный результат в виде списка
print(result)