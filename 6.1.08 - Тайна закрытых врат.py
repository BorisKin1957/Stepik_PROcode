'''Твой код должен:
Принять строку символов (разделённых пробелами).
Создать из них кортеж.
Проверить количество символов в кортеже:
Если меньше 3 → Вывести "Ошибка! Недостаточно символов!". Пункт 4 игнорировать!
Если от 3 до 6 символов → Оставить только три последних символа.
Если больше 6 символов → Оставить три первых и три последних символа.
Вывести полученный кортеж.'''

# Считываем строку, убираем все пробелы и преобразуем в кортеж символов
string = tuple(input().replace(' ', ''))

# Определяем длину кортежа
length = len(string)
# Флаг для отслеживания наличия ошибки (если меньше 3 символов)
flag = True

# Используем match-case для проверки длины кортежа
match length:
    case length if length < 3:
        # Если символов меньше 3 — устанавливаем флаг в False и сохраняем сообщение об ошибке
        error = 'Ошибка! Недостаточно символов!'
        flag = False
    case length if 3 <= length <= 6:
        # Если от 3 до 6 символов — оставляем только последние три
        string = string[-3:]
    case _:
        # Если больше 6 символов — берём первые три и последние три
        string = string[:3] + string[-3:]

# Выводим результат: либо кортеж (если всё в порядке), либо сообщение об ошибке
print(tuple(string) if flag else error)