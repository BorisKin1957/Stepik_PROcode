'''Твой код должен:

Принять строку с символами (разделёнными пробелом).
Создать из них кортеж.
Найти количество вхождений символа ☀️.
Найти  индекс символа, который встречается только один раз. Если такого нет,
определить как: -1
Вывести кортеж данных: (шаг 3, шаг 4)'''

# Принимаем строку от пользователя, разделяем её по пробелам и создаём кортеж из получённых элементов
symbols = tuple(input().split())

# Считаем количество вхождений символа '☀️' в кортеже
count = symbols.count('☀️')

# Создаём список кортежей: (индекс, количество вхождений элемента с этим индексом)
# Для каждого индекса i считаем, сколько раз элемент symbols[i] встречается в кортеже
result = [(i, symbols.count(symbols[i])) for i in range(len(symbols))]

# Фильтруем список result, оставляя только те пары (индекс, количество), где количество == 1
# То есть ищем элементы, которые встречаются ровно один раз
index = list(filter(lambda i: i[1] == 1, result))

# Если такие элементы найдены, выводим кортеж: (количество '☀️', индекс уникального элемента)
# Если нет — выводим (количество '☀️', -1)
print((count, index[0][0]) if index else (count, -1))
