# –ê–ª—Ñ–∞–≤–∏—Ç—ã –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ (–∑–∞—à–∏–≤—Ä–æ–≤–∞–Ω–Ω—ã–µ)
cirilic_low = '–∞–±–≤–≥–¥–µ—ë–∂–∑–∏–π–∫–ª–º–Ω–æ–ø—Ä—Å—Ç—É—Ñ—Ö—Ü—á—à—â—ä—ã—å—ç—é—è'[::-1]  # —Å—Ç—Ä–æ—á–Ω—ã–µ —Ä—É—Å—Å–∫–∏–µ –±—É–∫–≤—ã –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
cirilic_up = '–ê–ë–í–ì–î–ï–Å–ñ–ó–ò–ô–ö–õ–ú–ù–û–ü–†–°–¢–£–§–•–¶–ß–®–©–™–´–¨–≠–Æ–Ø'[::-1]  # –∑–∞–≥–ª–∞–≤–Ω—ã–µ —Ä—É—Å—Å–∫–∏–µ –±—É–∫–≤—ã –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ

latin_low = 'abcdefghijklmnopqrstuvwxyz'[::-1]  # —Å—Ç—Ä–æ—á–Ω—ã–µ –∞–Ω–≥–ª–∏–π—Å–∫–∏–µ –±—É–∫–≤—ã –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
latin_up = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'[::-1]  # –∑–∞–≥–ª–∞–≤–Ω—ã–µ –∞–Ω–≥–ª–∏–π—Å–∫–∏–µ –±—É–∫–≤—ã –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ

string = input()  # –ü–æ–ª—É—á–∞–µ–º –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
result = ''       # –ó–¥–µ—Å—å –±—É–¥–µ—Ç —Ö—Ä–∞–Ω–∏—Ç—å—Å—è —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç

# –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª –≤–æ –≤—Ö–æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ
for sym in string:
    if sym in cirilic_low:
        # –ó–∞–º–µ–Ω—è–µ–º —Å–∏–º–≤–æ–ª –Ω–∞ –ø—Ä–µ–¥—ã–¥—É—â–∏–π –≤ –æ–±—Ä–∞—Ç–Ω–æ–º —Ä—É—Å—Å–∫–æ–º –∞–ª—Ñ–∞–≤–∏—Ç–µ (—á—Ç–æ —ç–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–Ω–æ —Å–ª–µ–¥—É—é—â–µ–º—É –≤ –ø—Ä—è–º–æ–º)
        sym = cirilic_low[cirilic_low.index(sym) - 1]
    elif sym in cirilic_up:
        # –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ –¥–ª—è –∑–∞–≥–ª–∞–≤–Ω—ã—Ö —Ä—É—Å—Å–∫–∏—Ö –±—É–∫–≤
        sym = cirilic_up[cirilic_up.index(sym) - 1]
    elif sym in latin_low:
        # –î–ª—è —Å—Ç—Ä–æ—á–Ω—ã—Ö –∞–Ω–≥–ª–∏–π—Å–∫–∏—Ö –±—É–∫–≤
        sym = latin_low[latin_low.index(sym) - 1]
    elif sym in latin_up:
        # –î–ª—è –∑–∞–≥–ª–∞–≤–Ω—ã—Ö –∞–Ω–≥–ª–∏–π—Å–∫–∏—Ö –±—É–∫–≤
        sym = latin_up[latin_up.index(sym) - 1]
    # –î–æ–±–∞–≤–ª—è–µ–º –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–Ω—ã–π —Å–∏–º–≤–æ–ª –∫ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—É
    result += sym

# –í—ã–≤–æ–¥–∏–º —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
print(result)




