'''Реализуйте функцию calculate_final_price(base_price, discount=0, shipping_fee=0), где:

    base_price --> базовая стоимость заказа (число, обязательный параметр);

    discount --> процент скидки (число или строка; по умолчанию 0);

    shipping_fee --> стоимость доставки (число или строка; по умолчанию 0).

Функция должна вычислить:

final_price = base_price - (base_price * discount / 100) + shipping_fee

и вывести строку:

Итоговая сумма заказа: <final_price>
.
Особенности:

    Ввод и вызов функции скрыты; нужно только определить функцию, без input() и без явного вызова.

    Тесты передают discount и shipping_fee именованными аргументами.

    Если discount или shipping_fee приходят как пустая строка или строка из пробелов,
    внутри функции трактуйте их как 0. Если приходят как непустые строки с числами -->
    преобразуйте к числам.
'''


def calculate_final_price(base_price, discount=0, shipping_fee=0):
    # Проверяем, является ли discount строкой и состоит ли он только из цифр (без знаков, пробелов и т.п.)
    # Метод isdigit() возвращает False для пустых строк, строк с пробелами и отрицательных/дробных чисел
    if not discount.isdigit():
        discount = 0  # Если строка не содержит цифр или пустая — считаем скидку нулевой
    else:
        discount = float(discount)  # Если цифры есть — преобразуем в число с плавающей точкой

    # Аналогично обрабатываем shipping_fee
    if not shipping_fee.isdigit():
        shipping_fee = 0  # Если строка пустая, с пробелами или нечисловая — доставка бесплатно
    else:
        shipping_fee = float(shipping_fee)  # Иначе преобразуем в число

    # Преобразуем базовую цену в число с плавающей точкой (на случай, если пришла как строка)
    base_price = float(base_price)

    # Вычисляем итоговую стоимость:
    # вычитаем скидку (в процентах от base_price) и добавляем стоимость доставки
    final_price = base_price - (base_price * discount / 100) + shipping_fee

    # Выводим результат в требуемом формате
    print(f'Итоговая сумма заказа: {final_price}')


# Эти строки НЕ должны быть включены в финальное решение, так как:
# - задание говорит: "Ввод и вызов функции скрыты"
# - тесты сами вызывают функцию с именованными аргументами
# Однако они здесь для локального тестирования
price, disc, ship = input(), input(), input()  # Считываем входные данные от пользователя

calculate_final_price(price, disc, ship)  # Вызываем функцию с введёнными значениями