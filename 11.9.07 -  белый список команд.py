'''Шаг 1. Объяви фабрику декораторов allow_only(*commands).
Она принимает строки-команды и возвращает декоратор.
Шаг 2. Возвращаемый декоратор должен принимать функцию и возвращать обёртку wrapper.
Шаг 3. Обёртка должна ожидать, что первый аргумент функции это cmd.
Шаг 4. Если cmd входит в список разрешённых commands, обёртка вызывает исходную функцию
и возвращает её результат.
Шаг 5. Если cmd не входит в список, обёртка должна вернуть строку ровно:
Отклонено и не вызывать исходную функцию.
Шаг 6. Объяви функцию dispatch(cmd, value), которая возвращает строку формата <cmd>:<value>.
Ничего не печатает.
Шаг 7. Примени декоратор так, чтобы разрешёнными были команды PING, SET, GET.'''

# Определение фабрики декораторов, принимающей произвольное количество команд
def allow_only(*commands):
    # Внутренняя функция — сам декоратор, который будет применяться к функции
    def decorator(func):
        # Обёртка, заменяющая оригинальную функцию
        def wrapper(*args):
            # Проверяем, есть ли первый аргумент (предполагаем, что это cmd) в списке разрешённых команд
            if args[0] in commands:
                # Если да — вызываем оригинальную функцию с переданными аргументами и возвращаем результат
                return func(*args)
            # Если нет — возвращаем строку "Отклонено", не вызывая функцию
            return 'Отклонено'
        # Возвращаем обёртку из декоратора
        return wrapper
    # Возвращаем декоратор из фабрики
    return decorator

# Применяем декоратор @allow_only к функции dispatch, разрешая только команды 'PING', 'SET', 'GET'
@allow_only('PING', 'SET', 'GET')
def dispatch(cmd, value):
    # Функция возвращает строку в формате "<cmd>:<value>"
    return f'{cmd}:{value}'

# Вызов функции с разрешённой командой — вернёт 'PING:10'
print(dispatch('PING', '10'))

# Вызов функции с запрещённой командой — вернёт 'Отклонено'
print(dispatch('BOOT', '20'))