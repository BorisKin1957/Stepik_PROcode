'''Шаг 2. Считайте целое число N --> количество операций.
Шаг 3. Обработайте последующие N строк-команд:

    add X --> добавить фрукт X в множество;

    remove X --> удалить X из множества (если X отсутствует, возникает ошибка);

    discard X --> удалить X, если он есть (ошибки не возникает);

    pop --> удалить (и вернуть) случайный элемент множества (если множество пусто, возникает ошибка);

    clear --> полностью очистить множество.

Шаг 4. По завершении всех операций:

если множество пусто, выведите: Фруктовый сад пуст;

иначе выведите все оставшиеся фрукты одной строкой через пробел, отсортировав их в лексикографическом порядке.

🧪 Пример ввод

7
remove apple
pop
discard avocado
add apricot
pop
add coconut
clear

🧪 Пример вывод

Фруктовый сад пуст'''

# Изначальное множество из 20 фруктов
orchard = {
    "apple", "pear", "peach", "plum", "cherry",
    "banana", "orange", "lime", "lemon", "grapefruit",
    "mango", "papaya", "pineapple", "kiwi", "strawberry",
    "blueberry", "raspberry", "grape", "melon", "watermelon"
}

# Считываем количество команд
n = int(input())

# Выполняем каждую из n команд
for _ in range(n):
    # Считываем команду; используем моржовый оператор для одновременного чтения и проверки
    if (line := input().strip()) == 'pop':
        # Удаляем случайный элемент, если множество не пустое
        if orchard:
            orchard.pop()
        # Если множество пустое — ничего не делаем (ошибка игнорируется)

    elif line == 'clear':
        # Полностью очищаем множество
        orchard.clear()

    else:
        # Разделяем строку на команду и аргумент (например, 'add apple' -> ['add', 'apple'])
        command, fruit = line.split()

        if command == 'add':
            # Добавляем фрукт в множество
            orchard.add(fruit)

        elif command == 'remove' or command == 'discard':
            # Используем discard для безопасного удаления (не вызывает ошибку, если элемента нет)
            # Это покрывает оба случая: remove и discard
            orchard.discard(fruit)

# После всех операций проверяем, остался ли хотя бы один фрукт
if orchard:
    # Выводим отсортированные фрукты через пробел (лексикографический порядок)
    print(*sorted(orchard))
else:
    # Если множество пусто — выводим сообщение
    print('Фруктовый сад пуст')