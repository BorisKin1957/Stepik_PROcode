'''Шаг 1. Считайте целое число N --> количество записей.
Шаг 2. Считайте N строк. Каждая строка содержит название книги и год издания.
Название может содержать пробелы; год это последнее целое число в строке.
Шаг 3. С помощью filter() и анонимной функции lambda оставьте только книги, у которых год выпуска > 2000.
Шаг 4. Выведите итоговый список названий в одном Python-списке в порядке исходного ввода.'''

# Шаг 1: Считываем количество записей
n = int(input())

# Создаём список списков: каждая строка разбивается по пробелам
all_books = [input().split() for _ in range(n)]

# Шаг 3: Фильтруем книги, оставляя только те, у которых год (последний элемент) больше 2000
books = filter(lambda x: int(x[-1]) > 2000, all_books)

# Шаг 4: Преобразуем отфильтрованные записи обратно в названия (объединяем все части, кроме года)
result = list(map(lambda x: ' '.join(x[:-1]), books))

# Выводим результат — список названий книг, изданных после 2000 года
print(f'Книги, выпущенные после 2000 года:\n{result}')
