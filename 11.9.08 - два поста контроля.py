'''Шаг 1. Объяви декоратор Outer(func), который принимает функцию и возвращает обёртку wrapper.
Шаг 2. Обёртка декоратора Outer должна:
печатать строку OUT before до вызова функции;
вызывать исходную функцию;
печатать строку OUT after после вызова;
возвращать результат исходной функции.
Шаг 3. Объяви декоратор Inner(func) по той же логике, но с сообщениями IN before и IN after.
Шаг 4. Оба декоратора должны принимать и прокидывать *args, **kwargs.
Шаг 5. Объяви функцию core(a, b), которая возвращает a * b. Ничего не печатает.
Шаг 6. Примени декораторы к функции строго в таком порядке:

@Outer
@Inner
def core(a, b):
    ...'''

# Определение декоратора Outer — он принимает функцию `func`, которую нужно обернуть
def Outer(func):
    # Внутренняя функция-обёртка, которая заменит собой оригинальную функцию
    def wrapper(*args, **kwargs):
        # Печатаем сообщение ДО вызова оборачиваемой функции
        print('OUT before')
        # Вызываем оригинальную функцию с переданными аргументами и сохраняем результат
        result = func(*args, **kwargs)
        # Печатаем сообщение ПОСЛЕ вызова функции
        print('OUT after')
        # Возвращаем результат оригинальной функции
        return result
    # Декоратор возвращает обёртку (но не вызывает её!)
    return wrapper

# Определение декоратора Inner — аналогично Outer, но с другими сообщениями
def Inner(func):
    # Внутренняя обёртка
    def wrapper(*args, **kwargs):
        # Сообщение перед вызовом
        print('IN before')
        # Вызов оборачиваемой функции
        result = func(*args, **kwargs)
        # Сообщение после вызова
        print('IN after')
        # Возврат результата
        return result
    # Возвращаем обёртку
    return wrapper

# Применение декораторов:
# @Outer — внешний декоратор
# @Inner — внутренний декоратор (выполняется первым при вызове)
# Это эквивалентно: core = Outer(Inner(core))
@Outer
@Inner
def core(a, b):
    # Функция просто возвращает произведение двух чисел
    # Ничего не печатает — вся печать от декораторов
    return a * b

# Вызов функции core(1, 2)
# Будет применена цепочка: Outer -> Inner -> core
# Печать будет в порядке:
#   OUT before
#   IN before
#   IN after
#   OUT after
# Результат: 2
print(core(7, 2))