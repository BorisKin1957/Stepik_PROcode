


orders = (
    (101, (("Пицца", 2), ("Салат", 1), ("Кола", 3))),
    (102, (("Пицца", 1), ("Сок", 2))),
    (103, (("Салат", 2), ("Кофе", 1))),
    (104, (("Пицца", 3), ("Сок", 1), ("Кофе", 2))),
    (105, (("Кола", 2), ("Салат", 1), ("Пицца", 2)))
)


result = 0
dish = input()

for _, order in orders:
    for item in order:
        if dish == item[0]:
            result += item[1]

print(f'Общее количество заказов "{dish}": {result}')
