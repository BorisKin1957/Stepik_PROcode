'''🔹 Запроси у пользователя количество людей (целое число).

🔹 Далее по очереди прочитай цены на подарки для каждого человека (каждая цена это
отдельная строка, всего столько строк, сколько людей).

🔹 Применяй скидку 10% ко всем ценам с помощью анонимной функции (lambda).
То есть, новая цена = старая цена минус 10%.

🔹 Выведи итоговый список цен после применения скидки.
Все значения округли до двух знаков после запятой.

Используй функцию map() и анонимную функцию для применения скидки ко всем ценам разом.'''
'''
Можно в одну строку написать:
print(list(map(lambda x: round(x * 0.9, 2), [float(input()) for _ in range(int(input()))])))

Но мы же здесь для людей пишем? Так что оставим предыдущее извращенцам.
'''

# Запрашиваем у пользователя количество людей (целое число)
n = int(input())

# Создаём список: поочерёдно считываем n цен (как числа) из ввода
prices = [float(input()) for _ in range(n)]

# Определяем анонимную функцию (lambda), которая:
#   принимает x (цену), умножает её на 0.9 (т.е. вычитает 10% скидку),
#   затем округляет результат до 2 знаков после запятой
discounted_price = lambda x: round(x * 0.9, 2)

# Применяем функцию discount к каждому элементу списка prices с помощью map()
# Преобразуем результат map() в список и выводим его
result = list(map(discounted_price, prices))

print(result)