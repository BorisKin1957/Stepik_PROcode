'''Шаг 1. Считайте имя участника клуба.

Шаг 2. Если участник есть в базе, выведите две строки (база контактов уже есть в программе):
Телефон: <номер>
Адрес: <адрес>
Иначе выведите строку: Такого участника нет в клубе.

Шаг 3. Считайте цифру для фильтрации телефонных номеров.

Шаг 4. Выведите заголовок:
Результаты фильтрации по цифре '<цифра>':
Затем выведите все пары в формате <Имя>: <номер> для тех контактов, чьи номера начинаются
с этой цифры, в порядке обхода исходного словаря. Для этого используй генератор словаря.

Шаг 5. Если подходящих контактов нет, выведите строку:
Нет контактов, начинающихся с цифры '<цифра>'.'''

# Исходный словарь с контактами участников клуба.
# Каждый ключ — имя участника, значение — словарь с полями "phone" и "address".
club_contacts = {
    "Ivan": {
        "phone": "123-456-7890",  # Номер телефона Ивана
        "address": "1234 Pine St"  # Адрес Ивана
    },
    "Maria": {
        "phone": "987-654-3210",  # Номер Марии
        "address": "4321 Oak Ave"  # Адрес Марии
    },
    "Alex": {
        "phone": "555-789-4567",  # Номер Алекса
        "address": "5678 Elm Rd"   # Адрес Алекса
    },
    "John": {
        "phone": "111-222-3333",  # Номер Джона
        "address": "8765 Maple Ln"  # Адрес Джона
    },
    "Olga": {
        "phone": "222-333-4444",  # Номер Ольги
        "address": "7890 Birch Blvd"  # Адрес Ольги
    },
    "Nina": {
        "phone": "333-444-5555",  # Номер Нины
        "address": "6789 Cedar Dr"  # Адрес Нины
    },
    "Eugene": {
        "phone": "444-555-6666",  # Номер Евгения
        "address": "5432 Spruce St"  # Адрес Евгения
    },
    "Dmitry": {
        "phone": "555-666-7777",  # Номер Дмитрия
        "address": "2345 Fir Ct"   # Адрес Дмитрия
    },
    "Sofia": {
        "phone": "666-777-8888",  # Номер Софии
        "address": "1230 Maple Ave"  # Адрес Софии
    },
    "Anton": {
        "phone": "777-888-9999",  # Номер Антона
        "address": "4567 Pineapple Rd"  # Адрес Антона
    }
}

# Шаг 1: Считываем имя участника с клавиатуры
name = input()

# Шаг 2: Проверяем, есть ли введённое имя в словаре контактов
if name in club_contacts:
    # Если есть — выводим телефон и адрес этого участника
    print(f'Телефон: {club_contacts[name]["phone"]}')
    print(f'Адрес: {club_contacts[name]["address"]}')
else:
    # Если нет — сообщаем, что такого участника нет
    print('Такого участника нет в клубе')

# Шаг 3: Считываем цифру для фильтрации номеров
number = input()

# Шаг 4: Создаём новый словарь с помощью генератора
# Включаем только тех участников, чей номер начинается с введённой цифры
filtered_contacts = {k: v for k, v in club_contacts.items() if v["phone"][0] == number}

# Проверяем, есть ли отфильтрованные контакты
if filtered_contacts:
    # Если есть — выводим заголовок и список подходящих контактов
    print(f"Результаты фильтрации по цифре '{number}':")
    for k, v in filtered_contacts.items():
        print(f"{k}: {v['phone']}")
else:
    # Если нет — выводим сообщение об отсутствии совпадений
    print(f"Нет контактов, начинающихся с цифры '{number}'")
