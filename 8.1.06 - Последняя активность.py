'''1️⃣ Получить словарь IP-адресов → (ключ=IP, значение=словарь сайтов).
2️⃣ Определить IP с наибольшей активностью.
3️⃣ Найти самый посещаемый сайт этого IP.
4️⃣ Вывести IP-адрес и сайт.

Пример 1: (Обычный случай)
💻 Вход:

{"192.168.1.1": {"google.com": 5, "darkweb.net": 12, "forum.xyz": 7},
"10.0.0.23": {"google.com": 2, "darkweb.net": 1, "forum.xyz": 5}}

📊 Выход:

Самый активный IP: 192.168.1.1
Наиболее посещаемый сайт: darkweb.net'''

from json import \
    loads  # Импортируем функцию loads для преобразования строки JSON в Python-объект (словарь)

# Считываем JSON-словарь: ключи — хеши, значения — пароли
data = loads(
    input())  # Считываем строку от пользователя и преобразуем её из JSON в словарь

# Альтернативный закомментированный способ поиска самого активного IP через цикл
# total, i = 0, 0
#
# for ip, value in data.items():  # Перебираем все IP и их данные (сайты и количество посещений)
#     current_sum = sum(value.values())  # Суммируем количество посещений всех сайтов для текущего IP
#     if current_sum > total:  # Если сумма посещений больше предыдущей максимальной
#         total = current_sum  # Обновляем максимальную сумму
#         index = i  # Запоминаем индекс текущего IP
#     i += 1  # Увеличиваем счётчик
#
# ip = list(data)[index]  # Получаем IP по сохранённому индексу

# Находим IP с наибольшей суммой посещений сайтов
# Используем max() с ключом сортировки:
# x — это пара (ip, словарь_сайтов), x[1] — словарь сайтов
# sum([value for key, value in x[1].items()]) — сумма всех посещений у этого IP
# [0] — берём только сам IP (ключ), а не всю пару
ip = max(data.items(), key=lambda x: sum([value for key, value in x[1].items()]))[0]

print(f'Самый активный IP: {ip}')  # Выводим найденный IP с наибольшей активностью

# Ищем элемент с наибольшим количеством посещений и его ключ (название сайта)
site = max(data[ip].items(), key=lambda x: x[1])[0]

print(f'Наиболее посещаемый сайт: {site}')  # Выводим самый посещаемый сайт этого IP