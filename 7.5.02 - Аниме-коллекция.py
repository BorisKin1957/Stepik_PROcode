'''Шаг 1. Считайте данные о 5 аниме, по одному на строку, в формате:
Название: <...>, Количество серий: <...>, Жанр: <...>, Просмотрено: <True|False>.

Шаг 2. Выведите список «Просмотренные аниме:», затем названия всех тайтлов с Просмотрено:
True, по одному названию в строке.

Шаг 3. Выведите список «Аниме, которые хотите посмотреть:», затем названия всех тайтлов
с Просмотрено: False, по одному названию в строке.

Шаг 4. Выведите список «Аниме в жанре <жанр>:» для жанра, указанного пользователем,
затем названия подходящих тайтлов, по одному названию в строке.

Соблюдайте пустые строки между блоками вывода в точности как в примере'''

import sys  # Импортируем модуль sys для чтения данных из стандартного ввода

# Считываем весь ввод целиком, удаляем лишние пробеллы и разбиваем на строки
input_data = sys.stdin.read().strip().split('\n')

anime_data = {}  # Создаём пустой словарь для хранения информации об аниме

# Обрабатываем каждую строку входных данных
for line in input_data:
    # Убираем префиксы, чтобы извлечь значения
    line = line.replace('Название: ', '')
    line = line.replace('Количество серий: ', '')
    line = line.replace('Жанр: ', '')
    line = line.replace('Просмотрено: ', '')

    # Разделяем строку по запятым и извлекаем: название, количество серий, жанр, статус просмотра
    title, vol, genre, flag = line.split(', ')

    # Добавляем данные в словарь: ключ — название аниме, значение — словарь с информацией
    anime_data.setdefault(title, {'vol': vol, 'genre': genre, 'flag': flag})

# Выводим заголовок для просмотренных аниме
print('Просмотренные аниме:')

# Проходим по всем записям и выводим названия, где статус просмотра — True
for title, data in anime_data.items():
    if data['flag'] == 'True':
        print(title)

# Выводим пустую строку и заголовок для непросмотренных аниме
print('\nАниме, которые хотите посмотреть:')

# Проходим по всем записям и выводим названия, где статус просмотра — False
for title, data in anime_data.items():
    if data['flag'] == 'False':
        print(title)

# Выводим заголовок и пример жанра (в данном случае — Action)
print('\nАниме в жанре Action:')

# Проходим по всем записям и выводим названия, относящиеся к жанру "Action"
for title, data in anime_data.items():
    if data['genre'] == 'Action':
        print(title)