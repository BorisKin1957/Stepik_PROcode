'''Шаг 1. Считайте количество роботов N.
Шаг 2. Считайте N строк с данными роботов в формате: <вес> <мощность>.
Шаг 3. С помощью map() и lambda увеличьте мощность каждого робота на 20% (умножьте на 1.2).
Шаг 4. С помощью filter() и lambda оставьте только тех роботов, у которых новая мощность не меньше 100.
Шаг 5. Отсортируйте оставшихся роботов с помощью sorted() и lambda по весу (по возрастанию).
Шаг 6. Округлите итоговую мощность до 2 знаков после запятой в результирующих данных.
Шаг 7. Выведите результат как Python-список кортежей (вес, мощность).

Требования: использовать только анонимные функции (lambda) вместе с map(), filter(), sorted()
для соответствующих шагов.'''

data = [input().split() for _ in range(int(input()))]

robots = [(int(weight), round(int(power) * 1.2, 2)) for weight, power in data]

result = sorted(filter(lambda p: p[1] >= 100, robots))

print(result)