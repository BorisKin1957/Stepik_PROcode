'''Считайте одну строку с несколькими элементами, разделёнными пробелами.
Для каждого элемента определите тип и выведите его на новой строке.
Допустимые типы:
целое число → int;
вещественное число с точкой (например, 0.5, 99.99) → float;
булево значение True или False (без кавычек) → bool;
строка в двойных кавычках → str.
Если элемент нельзя отнести ни к одному из допустимых типов, выведите:
Неверный тип данных'''

import re  # Импортируем модуль для работы с регулярными выражениями


def get_type_date(date):
    # Проверяем, соответствует ли строка шаблону:
    # начинается и заканчивается на ",
    # между ними — любые символы (включая пустую строку)
    if re.fullmatch(r'^".*"$', date):
        return 'str'

    # Проверяем, является ли строка целым числом:
    # необязательный минус, затем одна или более цифр
    if re.fullmatch(r'^-?\d+$', date):
        return 'int'

    # Проверяем, является ли строка вещественным числом:
    # необязательный минус, одна или более цифр, точка,
    # одна или более цифр (например: 3.14, -0.5)
    if re.fullmatch(r'^-?\d+\.\d+$', date):
        return 'float'

    # Проверяем, является ли строка булевым значением:
    # точно "True" или "False"
    if re.fullmatch(r'True|False', date):
        return 'bool'

    # Если ни одно условие не подошло — неверный тип данных
    return 'Неверный тип данных'


# Считываем входную строку, разбиваем её по пробелам
# и для каждого слова определяем тип
for word in input().split():
    print(get_type_date(word))


