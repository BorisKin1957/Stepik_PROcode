'''На вход подаётся строка с идентификаторами, разделёнными пробелами.
Требуется преобразовать их так, чтобы каждое повторное появление одного и того
же идентификатора получало постфикс _<n>, где n --> число предыдущих вхождений
этого идентификатора (начиная с 1). Первое появление идентификатора не изменяется.
Выведите исправленную строку, сохранив исходный порядок.

🧪 Пример ввод

агентX агентY агентX агентZ агентY агентX

🧪 Пример вывод

агентX агентY агентX_1 агентZ агентY_1 агентX_2
'''

# Считываем входную строку и разбиваем её на список идентификаторов по пробелам
string = input().split()

# Словарь для отслеживания количества уже встреченных вхождений каждого идентификатора
identifiers = {}

# Список для хранения результирующих идентификаторов (с постфиксами при необходимости)
result = []

# Перебираем каждый идентификатор во входной последовательности
for ident in string:
    # Если идентификатор встречается впервые
    if ident not in identifiers:
        # Добавляем его в словарь с начальным значением счётчика 0
        identifiers.setdefault(ident, 0)
        # В результат добавляем сам идентификатор без изменений
        result.append(ident)
    else:
        # Если идентификатор уже встречался — увеличиваем счётчик
        identifiers[ident] += 1
        # Формируем строку с постфиксом: например, "агентX_1"
        result.append(f'{ident}_{identifiers[ident]}')

# Выводим результат как строку, разделяя элементы пробелом
print(*result)