'''Шаг 1. Считайте целое число n --> количество номеров на складе.

Шаг 2. Считайте одну строку с n целыми числами --> текущие номера (повторы возможны).

Шаг 3. Создайте множество parts из этих номеров.

Шаг 4. Создайте переменную strict_remove и запишите в неё метод parts.remove.

Шаг 5. Создайте переменную soft_remove и запишите в неё метод parts.discard.

Шаг 6. Считайте целое число k --> сколько номеров нужно удалить строго.

Шаг 7. Считайте одну строку с k целыми числами и удалите их через strict_remove(x).
Гарантируется, что каждый такой номер есть в множестве.

Шаг 8. Считайте целое число t --> сколько номеров нужно удалить мягко.

Шаг 9. Считайте одну строку с t целыми числами и удалите их через soft_remove(x).
Этих номеров может не быть в множестве.

Шаг 10. Выведите:
если parts пустое --> Пусто
иначе --> элементы parts в отсортированном виде через пробел'''

# Считываем количество номеров на складе (не используется напрямую, но нужно для согласованности ввода)
input()

# Считываем строку с номерами, разбиваем её на части, преобразуем в целые числа и создаём множество (удаляем дубликаты)
parts = set(map(int, input().split()))

# Сохраняем метод .remove множества parts в переменную strict_remove
# Метод .remove удаляет элемент, но вызывает ошибку, если элемента нет
strict_remove = parts.remove

# Сохраняем метод .discard множества parts в переменную soft_remove
# Метод .discard удаляет элемент, если он есть, и ничего не делает, если его нет
soft_remove = parts.discard

# Считываем количество номеров, которые нужно удалить строго (гарантированно присутствуют в множестве)
k, tmp = int(input()), [int(num) for num in input().split()]

# Удаляем k номеров с помощью strict_remove (будет работать, так как гарантируется наличие элементов)
for i in range(k):
    strict_remove(tmp[i])

# Считываем количество номеров, которые нужно удалить мягко (могут отсутствовать)
t = int(input())

# Если нужно удалить хотя бы один номер мягко — считываем их и удаляем через discard
if t:
    tmp = [int(num) for num in input().split()]
    for i in range(t):
        soft_remove(tmp[i])

# Проверяем, остался ли хотя бы один элемент в множестве
if parts:
    # Выводим отсортированные оставшиеся номера через пробел
    print(*sorted(parts))
else:
    # Если множество пустое — выводим "Пусто"
    print('Пусто')

