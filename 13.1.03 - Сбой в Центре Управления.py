'''Шаг 1. Объяви функцию matrix_add(a, b), где a и b --> матрицы (списки списков
однотипной прямоугольной формы).
Шаг 2. Если размеры матриц не совпадают (разное число строк или столбцов), верни строку ровно:
Матрицы разного размера - сложение невозможно.
Шаг 3. Иначе верни новую матрицу --> поэлементную сумму a[i][j] + b[i][j].'''

def matrix_add(a: list[list[int]], b: list[list[int]]) -> list[list[int]] | str:
    # Получаем длины строк (количество столбцов) для каждой строки матрицы a
    size_a = [len(a[i]) for i in range(len(a))]
    # Получаем длины строк (количество столбцов) для каждой строки матрицы b
    size_b = [len(b[i]) for i in range(len(b))]

    # Проверяем, совпадают ли размеры всех строк внутри каждой матрицы и между матрицами
    # Если все строки в a и b имеют одинаковую длину и их количества совпадают, то размеры матриц одинаковы
    if size_a == size_b:
        len1 = len(a)      # количество строк в матрице a
        len2 = len(a[0])   # количество столбцов в матрице a (предполагается, что матрица прямоугольная)

        # Проходим по каждой ячейке матрицы и добавляем элементы из b к соответствующим элементам a
        for i in range(len1):
            for j in range(len2):
                a[i][j] += b[i][j]  # изменяем матрицу a на месте

        return a  # возвращаем изменённую матрицу a как результат
    else:
        # Если размеры матриц не совпадают, возвращаем сообщение об ошибке
        return 'Матрицы разного размера - сложение невозможно.'

# Тестовые примеры:
# Пример 1: Сложение двух одинаковых матриц 2x3
A = [
    [1, 2, 3],
    [4, 5, 6]
]

B = [
    [7, 8, 9],
    [10, 11, 12]
]
print(matrix_add(A, B))  # Ожидается: [[8,10,12], [14,16,18]]

# Пример 2: Повторный вызов с теми же данными (но A уже изменена!)
print(matrix_add(A, B))  # Будет ошибка логики, так как A была изменена ранее

# Пример 3: Матрицы с разным количеством столбцов → ошибка
A = [
    [1, 2],
    [3, 4]
]

B = [
    [5, 6, 7],
    [8, 9, 10]
]
print(matrix_add(A, B))  # Ожидается: "Матрицы разного размера..."

