'''В программе задан вложенный кортеж поставок:

deliveries = (
    ("Поставщик А", ("яблоко", "банан", "киви", "яблоко", "груша", "яблоко", "банан")),
    ("Поставщик В", ("груша", "яблоко", "банан", "яблоко", "киви", "яблоко")),
    ("Поставщик С", ("киви", "банан", "яблоко", "груша", "банан", "яблоко", "яблоко", "груша"))
)


Считай одну строку --> название фрукта (на кириллице), для которого нужно собрать статистику (сравнение --> по точному совпадению строки).

Выполни подсчёт:

для каждого поставщика --> сколько раз этот фрукт встречается в его поставках;

общего количества по всем поставщикам.

Выведи:

если общий счёт больше нуля:

Общее количество товара "<фрукт>" среди всех поставщиков: <число>
Статистика по поставщикам:
  Поставщик А: <число> раз(а)
  Поставщик В: <число> раз(а)
  Поставщик С: <число> раз(а)


(обрати внимание: перед строками с поставщиками --> ровно 2 пробела)

если фрукт не встречается ни у одного поставщика:

Товар "<фрукт>" отсутствует в доставках.'''

# Определение вложенного кортежа `deliveries`, содержащего данные о поставках от разных поставщиков.
# Каждый элемент — это кортеж из двух элементов:
#   - название поставщика (строка),
#   - кортеж с названиями фруктов, которые он поставил.
deliveries = (
    ("Поставщик А", ("яблоко", "банан", "киви", "яблоко", "груша", "яблоко", "банан")),
    ("Поставщик В", ("груша", "яблоко", "банан", "яблоко", "киви", "яблоко")),
    ("Поставщик С", ("киви", "банан", "яблоко", "груша", "банан", "яблоко", "яблоко", "груша"))
)

# Считываем с клавиатуры название фрукта, для которого нужно провести анализ.
# Пользователь вводит строку (например, "яблоко"), и она сохраняется в переменную `fruit`.
fruit = input()

# Создаём пустой список `results`, в который будем складывать пары:
# (название поставщика, количество поставок указанного фрукта у этого поставщика).
results = []

# Начинаем цикл по каждому элементу кортежа `deliveries`.
# На каждой итерации:
#   - `delivery` — название поставщика (например, "Поставщик А"),
#   - `fruits` — кортеж с фруктами, которые он поставил.
for delivery, fruits in deliveries:
    # Проверяем, встречается ли введённый фрукт (`fruit`) хотя бы раз в поставках текущего поставщика.
    if fruit in fruits:
        # Если да, считаем точное количество вхождений этого фрукта с помощью метода `.count()`
        # и добавляем кортеж (название поставщика, количество) в список `results`.
        results.append((delivery, fruits.count(fruit)))

# Проверяем, есть ли какие-либо результаты (т.е. встречался ли фрукт хотя бы у одного поставщика).
if results:
    # Считаем общее количество поставок фрукта: суммируем все значения количества из списка `results`.
    # Используем `map` для извлечения второго элемента (количества) из каждой пары,
    # затем суммируем их с помощью `sum()`.
    total = sum(map(lambda x: x[1], results))

    # Выводим общее количество найденных поставок фрукта.
    print(f'Общее количество товара "{fruit}" среди всех поставщиков: {total}')

    # Выводим заголовок статистики по поставщикам.
    print('Статистика по поставщикам:')

    # Перебираем все элементы списка `results` (поставщик и количество).
    for delivery, number in results:
        # Выводим строку с двумя пробелами в начале, названием поставщика и количеством поставок.
        # Формат: "  Поставщик А: 3 раз(а)"
        print(f'  {delivery}: {number} раз(а)')
else:
    # Если фрукт нигде не найден (`results` пуст), выводим сообщение об отсутствии товара.
    print(f'Товар "{fruit}" отсутствует в доставках.')