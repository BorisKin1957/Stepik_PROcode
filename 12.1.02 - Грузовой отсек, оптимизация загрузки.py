'''
✅ Написать тело функции загрузить_груз(**kwargs).
✅ Вернуть(return) контейнеры и их содержимое в виде строк заданного формата.
✅ Функцию вызывать не нужно.

Ограничение: не используйте set, range

Sample Input:
секция4="Редкие минералы", секция2="Оружие", секция1="Патроны"

Sample Output:
Контейнер 1: "Патроны"
Контейнер 2: "Оружие"
Контейнер 4: "Редкие минералы"
'''

import re


def загрузить_груз(**kwargs):
    # Словарь для хранения номера контейнера (ключ) и содержимого (значение)
    result = {}

    # Перебираем все переданные именованные аргументы
    for key, value in kwargs.items():
        # Ищем первую цифру в названии ключа (например, в "секция4" — это '4')
        digit = re.search(r'\d', key)
        if digit:
            # Получаем найденную цифру как строку
            number = digit.group()
            # Находим индекс этой цифры в строке ключа
            index = key.index(number)
            # Извлекаем подстроку с этого индекса до конца (это будет номер)
            # Например, из "секция4" берём "4", из "box12" — "12"
            result[int(key[index:])] = value

    # Сортируем словарь по ключам (по номерам контейнеров)
    sorted_result = sorted(result.items(), key=lambda item: item[0])

    # Подготовка финальной строки результата
    result = ''

    # Формируем строки нужного формата
    for key, value in sorted_result:
        result += f'Контейнер {key}: {value}\n'

    # Возвращаем результат без последнего перевода строки
    return result.strip('\n')


