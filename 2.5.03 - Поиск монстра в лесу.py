'''Считайте одну строку --> список имён монстров, разделённых пробелами.
Найдите первого монстра, который встречается в списке ровно один раз.
Если такого нет, выведите: Нет новых монстров.'''

# Считываем строку от пользователя и разбиваем её на список имён монстров по пробелам
monsters = input().split()

# Инициализируем счётчик, который будет считать, сколько монстров уже проверили
# (и не являются уникальными — то есть встречаются более одного раза)
count = 0

# Проходим по каждому монстру в списке
for monster in monsters:
    # Проверяем, сколько раз текущий монстр встречается в списке
    if monsters.count(monster) == 1:
        # Если монстр встречается ровно один раз — это искомый "новый" монстр
        print(monster)  # Выводим его имя
        break  # Прерываем цикл, так как нужно найти только первого такого монстра
    else:
        # Если монстр встречается больше одного раза, увеличиваем счётчик проверенных
        count += 1

# После цикла проверяем: если все монстры были просмотрены и ни один не оказался уникальным
if count == len(monsters):
    # Это означает, что ни один монстр не встречается ровно один раз
    print('Нет новых монстров')

