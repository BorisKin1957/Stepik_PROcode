'''
Пользователь сначала вводит количество зон (строк).
Затем для каждой зоны вводится строка из трёх целых чисел, разделённых пробелами (координаты).
Пример:
3
1 2 3
4 5 6
7 8 9

Нужно:
    Сформировать вложенный список coordinates.
    Вывести всю карту.
    Вывести координату из второй зоны, первая позиция. Если такой нет, вывести:
    Недостаточно зон для доступа к [1][0]
    Найти зону с наибольшей суммой координат, и вывести эту зону.
    Найти максимальную точку на всей карте и вывести её.
'''

# Считываем количество зон
# Затем для каждой зоны считываем строку, разбиваем по пробелам и преобразуем элементы в целые числа
# Результат — вложенный список coordinates
coordinates = [[int(i) for i in input().split()] for j in range(int(input()))]

# Выводим весь список координат (карту)
print(coordinates)

# Пытаемся получить элемент с индексом [1][0] — вторая зона, первое число
# Если зон меньше двух, возникнет IndexError
try:
    print(f'Координата [1][0]: {coordinates[1][0]}')
except IndexError:
    print('Недостаточно зон для доступа к [1][0]')

# Находим зону (вложенный список), сумма элементов которой максимальна
zone_max = max(coordinates, key=lambda x: sum(x))
print(f'Зона с максимальной суммой: {zone_max}')

# Объединяем все подсписки в один плоский список (с помощью sum(coordinates, [])),
# затем находим максимальное значение
max_coord = max(sum(coordinates, []))
print(f'Максимальная координата: {max_coord}')