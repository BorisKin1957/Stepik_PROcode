'''Шаг 1. Объявите декоратор validate_positive(), который принимает функцию и возвращает обёртку.
Обёртка должна проверить единственный числовой параметр n:

    если n > 0, выполнить исходную функцию и вернуть её результат;

    если n <= 0, вернуть строку ровно: Ошибка: число должно быть больше нуля!

Шаг 2. Объявите функцию square_root(n), которая вычисляет квадратный корень из n и
округляет результат до 2 знаков после запятой (используйте round(..., 2)).
Ничего не печатает --> только return.

Шаг 3. Примените декоратор к функции с помощью @validate_positive.'''


'''Декоратор validate_positive() проверяет, что числовой аргумент функции больше нуля.
Если условие выполняется — вызывает функцию, иначе возвращает сообщение об ошибке.'''

# Объявление декоратора: принимает функцию `func` как аргумент
def validate_positive(func):
    # Внутренняя функция-обёртка, которая будет заменять оригинальную функцию
    def wrapper(num):
        # Проверяем, если переданное число меньше или равно нулю
        if num <= 0:
            # Возвращаем строку с точным текстом ошибки
            return 'Ошибка: число должно быть больше нуля!'
        # Если число положительное, вызываем оригинальную функцию с этим числом
        return func(num)
    # Декоратор возвращает обёртку (функцию), а не результат её вызова
    return wrapper

# Применение декоратора к функции square_root с помощью синтаксиса @
@validate_positive
def square_root(n: int) -> float:
    # Вычисляем квадратный корень из n с помощью возведения в степень 1/2
    # Результат округляем до 2 знаков после запятой
    return round(n ** (1 / 2), 2)

# Тестирование функции с положительным числом — должно работать
print(square_root(12))   # Вывод: например, 3.46

# Тестирование с отрицательным числом — должен сработать декоратор и вернуть ошибку
print(square_root(-100)) # Вывод: Ошибка: число должно быть больше нуля!