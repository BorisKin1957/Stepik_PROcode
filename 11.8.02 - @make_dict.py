# Определение декоратора make_dict, который принимает функцию `func` в качестве аргумента
def make_dict(func):
    # Внутренняя функция-обёртка, которая будет заменять собой оригинальную функцию
    def wrapper(*args, **kwargs):
        # Вызываем оригинальную функцию (split_words) и получаем кортеж из двух списков: ключи и значения
        keys, values = func(*args, **kwargs)
        # Объединяем два списка в словарь: элементы первого списка становятся ключами,
        # элементы второго — соответствующими значениями (с помощью zip и dict)
        return dict(zip(keys, values))
    # Возвращаем обёртку, чтобы она вызывалась при использовании декорированной функции
    return wrapper


# Применение декоратора @make_dict к функции split_words
@make_dict
def split_words(str1: str, str2: str) -> tuple[list]:
    '''Функция принимает две строки, разбивает каждую на слова с помощью split(),
    и возвращает кортеж из двух списков слов.'''
    # Разбиваем первую строку на список слов по пробелам
    # Разбиваем вторую строку на список слов по пробелам
    # Возвращаем кортеж: (список слов из str1, список слов из str2)
    return str1.split(), str2.split()


# Считываем две строки с клавиатуры
s1, s2 = input(), input()

# Вызываем декорированную функцию split_words:
# 1. Она сначала выполняет split_words(s1, s2) → получает два списка слов
# 2. Затем декоратор make_dict преобразует эти два списка в словарь
# 3. Результат (словарь) выводится на экран
print(split_words(s1, s2))