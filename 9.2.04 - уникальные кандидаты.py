'''Шаг 1. Входные данные состоят из нескольких строк формата
имя кандидата: комментарий
(имя может содержать пробелы; комментарий --> любые символы).
Шаг 2. Для каждой строки выделите имя кандидата как подстроку до первого символа :
(пробелы по краям имени игнорируйте).
Шаг 3. Определите количество уникальных имён кандидатов и выведите одно число.'''


# Импортируем модуль sys для чтения входных данных из стандартного ввода
import sys

# Считываем все строки из стандартного ввода, убираем пробельные символы с начала и конца каждой строки
lst = list(map(str.strip, sys.stdin.readlines()))

# Извлекаем имена кандидатов: берём часть строки до двоеточия и удаляем лишние пробелы
names = [item[:item.index(':')].strip() for item in lst]

# Преобразуем список имён в множество (чтобы оставить только уникальные имена) и выводим их количество
print(len(set(names)))