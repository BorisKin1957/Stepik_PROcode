'''Шаг 1. Объявите функцию calculate_area(width, height), которая возвращает произведение
width * height. Ничего не печатает.
Шаг 2. Объявите декоратор display_result(func), который принимает функцию и возвращает
обёртку. Обёртка должна:
— вызывать исходную функцию с переданными аргументами;
— получить её числовой результат;
— распечатать ровно строку: 📏 Площадь помещения: <значение> м² (обратите внимание
на пробел перед м²);
— вернуть полученный результат (на случай дальнейшего использования).'''

# Определение декоратора display_result, который принимает функцию `func` в качестве аргумента
def display_result(func):
    # Внутренняя функция-обёртка, которая принимает любые позиционные (*args) и именованные (**kwargs) аргументы
    def wrapper(*args, **kwargs):
        # Вызов оригинальной функции с переданными аргументами и сохранение результата в переменную `result`
        result = func(*args, **kwargs)
        # Вывод строки с результатом в формате: 📏 Площадь помещения: <значение> м²
        print(f'📏 Площадь помещения: {result} м²')
        # Возврат результата для возможного дальнейшего использования
        return result
    # Декоратор возвращает обёртку (функцию), а не результат её вызова
    return wrapper

# Функция calculate_area принимает два параметра: width и height
# Она вычисляет и возвращает площадь как произведение ширины на высоту
# Обратите внимание: строка с @display_result закомментирована — значит, декоратор не применяется напрямую
#@display_result
def calculate_area(width, height):
    return width * height

# Считываем с консоли два числа (ширину и высоту), разделяя ввод пробелом, и преобразуем их в целые числа
w, h = map(int, input().split())

# Вручную применяем декоратор к функции calculate_area
# Создаём новую функцию `decorated`, которая уже включает логику декоратора
decorated = display_result(calculate_area)

# Вызываем декорированную функцию с аргументами w и h
# Это приведёт к вычислению площади, выводу сообщения и возврату результата
decorated(w, h)