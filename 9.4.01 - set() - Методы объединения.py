'''Шаг 1. Считайте три строки входных данных:
первая --> номера сторонников партии собак (через пробел),
вторая --> партии кошек,
третья --> партии попугаев.
Каждая строка формирует множество (повторы в строке игнорируются). Пустая строка означает пустое множество.

Шаг 2. Выведите сами множества сторонников в точности в таком формате (элементы по возрастанию, через запятую и пробел; пустое множество как set()):

Сторонники партии собак: {…}
Сторонники партии кошек: {…}
Сторонники партии попугаев: {…}

Шаг 3. Выведите количества сторонников в каждой партии:

Общее количество сторонников партии собак: <число>
Общее количество сторонников партии кошек: <число>
Общее количество сторонников партии попугаев: <число>

Шаг 4. Выведите попарные пересечения (тот же формат множества, элементы по возрастанию):

Общие сторонники между собаками и кошками: {…}
Общие сторонники между собаками и попугаями: {…}
Общие сторонники между кошками и попугаями: {…}

Шаг 5. Определите победителя по наибольшему числу сторонников и выведите одну из строк:

    Победила партия собак с наибольшим количеством сторонников! 🐶🎉

    Победила партия кошек с наибольшим количеством сторонников! 🐱🎉

    Победила партия попугаев с наибольшим количеством сторонников! 🦜🎉
    Если все три партии набрали одинаковое количество сторонников, выведите:
    Ой-ой! Похоже, выборы были отменены! Все партии набрали одинаковое количество сторонников! 😹
'''

# Считываем строки и преобразуем в списки целых чисел
dogs = sorted(map(int, input().split()))
cats = sorted(map(int, input().split()))
parrots = sorted(map(int, input().split()))

# Выводим множества сторонников (в виде отсортированных списков)
print(f'Сторонники партии собак: {{{", ".join(str(i) for i in dogs)}}}')
print(f'Сторонники партии кошек: {{{", ".join(str(i) for i in cats)}}}')
print(f'Сторонники партии попугаев: {{{", ".join(str(i) for i in parrots)}}}')

# Подсчитываем количество сторонников каждой партии
d = len(dogs)
c = len(cats)
p = len(parrots)

print(f'Общее количество сторонников партии собак: {d}')
print(f'Общее количество сторонников партии кошек: {c}')
print(f'Общее количество сторонников партии попугаев: {p}')

# Преобразуем списки в множества для операций пересечения
dogs, cats, parrots = set(dogs), set(cats), set(parrots)

# Находим пересечения между парами множеств и сортируем результаты
dc = sorted(dogs & cats)
dp = sorted(dogs & parrots)
cp = sorted(cats & parrots)

# Выводим пересечения, проверяя, пусты ли они
if dc:
    print(f'Общие сторонники между собаками и кошками: {{{", ".join(str(i) for i in dc)}}}')
else:
    print('Общие сторонники между собаками и кошками: set()')

if dp:
    print(f'Общие сторонники между собаками и попугаями: {{{", ".join(str(i) for i in dp)}}}')
else:
    print('Общие сторонники между собаками и попугаями: set()')

if cp:
    print(f'Общие сторонники между кошками и попугаями: {{{", ".join(str(i) for i in cp)}}}')
else:
    print('Общие сторонники между кошками и попугаями: set()')

# Определяем победителя по максимальному количеству сторонников
if d == c == p:
    print('Ой-ой! Похоже, выборы были отменены! Все партии набрали одинаковое количество сторонников! 😹')
elif d > c and d > p:
    print('Победила партия собак с наибольшим количеством сторонников! 🐶🎉')
elif c > d and c > p:
    print('Победила партия кошек с наибольшим количеством сторонников! 🐱🎉')
elif p > d and p > c:
    print('Победила партия попугаев с наибольшим количеством сторонников! 🦜🎉')
else:
    # Ничья между двумя партиями — третья не больше
    print('Избирательная комиссия накосячила в подсчете голосов!')