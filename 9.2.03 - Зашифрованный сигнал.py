'''Шаг 1. Считайте одну строку текста (возможны латинские буквы, пробелы, цифры и другие символы).
Шаг 2. Выделите все цифры от 0 до 9, которые встречаются в строке, оставив каждую только один раз.
Шаг 3. Отсортируйте найденные цифры по возрастанию и выведите их через пробел в одной строке.
Шаг 4. Если в строке нет цифр, выведите ровно: Цифр нет'''

# Импортируем функцию findall из модуля re (регулярные выражения)
from re import findall

# Считываем входную строку от пользователя
# findall(r'\d+', input()) — находит все подстроки из одной или более цифр (например, "123", "45")
# ''.join(...) — объединяет все найденные цифровые подстроки в одну строку
# set(...) — создаёт множество, убирая дубликаты цифр
numbers = set(''.join(findall(r'\d+', input())))

# Проверяем, есть ли найденные цифры
if numbers:
    # Если цифры есть — сортируем их по возрастанию (в алфавитном порядке, что для цифр 0-9 = числовому)
    # *sorted(numbers) — распаковывает отсортированные цифры как отдельные аргументы в print
    # print автоматически выводит их через пробел (по умолчанию sep=' ')
    print(*sorted(numbers))
else:
    # Если цифр нет — выводим строго заданное сообщение
    print('Цифр нет')