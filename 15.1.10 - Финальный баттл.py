'''
✅ Объявить функцию analyze_battle(text).
✅ Разбить строку на слова.
✅ Посчитать общее количество слов.
✅ Посчитать количество уникальных слов.
✅ Найти самое частое слово. Если их несколько вывести первое!
✅ Вернуть кортеж: (всего_слов, уникальных_слов, самое_частое_слово).
'''


def analyze_battle(text: str) -> tuple[int, str]:
    # Разделяем строку на слова по пробелам и убираем лишние пробелы вокруг каждого слова
    words = [word.strip() for word in text.split()]

    # Считаем количество уникальных слов, преобразуя список в множество
    unique = len(set(words))

    # Создаём словарь: слово -> сколько раз оно встречается в списке
    # Затем находим пару (слово, частота) с максимальной частотой, берём ключ [0] от этой пары
    frequent = max({word: words.count(word) for word in words}.items(), key=lambda x: x[1])[0]

    # Возвращаем кортеж: (всего слов, уникальных слов, самое частое слово)
    return len(words), unique, frequent


# Пример вызова функции с тестовой строкой
print(analyze_battle(
    "Это мой вход мой main мой старт Мои слова как byte-код остры как Dart"))