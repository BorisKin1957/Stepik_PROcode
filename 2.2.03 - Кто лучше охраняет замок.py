'''Считайте два целых числа (каждое с новой строки):
площадь участка Виктора;
площадь участка Михаила.

Выведите одну из строк:
- "Виктор охраняет больше", если площадь Виктора больше площади Михаила;
- "Михаил охраняет больше", если площадь Михаила больше площади Виктора;
- "Они охраняют одинаковые участки", если площади равны.

Если оба участка не больше 100 кв. м, дополнительно выведите:
"Ни у кого из стражей нет участка больше 100 квадратных метров"
'''

# Считываем площадь участка Виктора
viktor_square = int(input())

# Считываем площадь участка Михаила
mihail_square = int(input())

# Словарь с текстовыми сообщениями для вывода
texts = {
    1: 'Виктор охраняет больше',
    2: 'Михаил охраняет больше',
    3: 'Они охраняют одинаковые участки',
    4: 'Ни у кого из стражей нет участка больше 100 квадратных метров'
}

# Сравниваем площади и выводим соответствующее сообщение
if viktor_square > mihail_square:
    print(texts[1])  # Виктор охраняет больше
elif viktor_square < mihail_square:
    print(texts[2])  # Михаил охраняет больше
else:
    print(texts[3])  # Участки равны

# Проверяем, не превышает ли каждый участок 100 кв.м
if viktor_square <= 100 and mihail_square <= 100:
    print(texts[4])  # Оба участка маленькие