'''В программе уже создан словарь с именами и кодами бандитов:

bandits = {"Джек": "+7", "Виктор": "+6", "Артур": "+5"}


Считай из ввода одно имя бандита.

Если имя есть в словаре → выведи в таком формате:
Код <имя>: <код>

Если имени нет → сначала выведи строку:
<имя> не найден, введите новый код:
затем считай второй ввод (код), добавь пару <имя>: <код> в словарь и выведи:
<имя> добавлен с кодом <код>.'''

# Исходный словарь: хранит имена бандитов (ключи) и их коды (значения)
bandits = {"Джек": "+7", "Виктор": "+6", "Артур": "+5"}

# Используем моржовый оператор (:=), чтобы:
# 1. Считать имя с помощью input()
# 2. Присвоить его переменной name
# 3. Немедленно проверить — есть ли это имя среди ключей словаря bandits
# Обрати внимание: in bandits — эквивалентно in bandits.keys(), короче и чище
if (name := input()) in bandits:
    # Если имя найдено — выводим код в требуемом формате
    print(f'Код {name}: {bandits[name]}')
else:
    # Если имя не найдено — уведомляем пользователя
    print(f'{name} не найден, введите новый код:')

    # Считываем код и сразу добавляем новую пару в словарь
    # Прямое присваивание bandits[name] = input() проще и понятнее, чем setdefault
    bandits[name] = input()

    # Подтверждаем успешное добавление
    print(f'{name} добавлен с кодом {bandits[name]}.')
