'''🔹 Напиши функцию speed_booster(), которая внутри себя создаёт вложенную функцию
с именем boost(speed).

🔹 Вложенная функция boost принимает одно число --> скорость передачи данных и возвращает
это число, увеличенное на 10% (результат округляется до двух знаков после запятой).

🔹 Основная функция speed_booster() должна возвращать ссылку на функцию boost.
То есть, создай замыкание: возвращаемая функция должна помнить всё необходимое для работы.'''

def speed_booster():
    # Основная функция, которая создаёт и возвращает замыкание
    def boost(speed):
        # Вложенная функция, которая принимает значение speed
        speed *= 1.1          # Увеличиваем скорость на 10%
        return round(speed, 2) # Округляем результат до двух знаков после запятой и возвращаем

    return boost  # Возвращаем саму функцию boost (без вызова) — это и есть замыкание

# Создаём функцию f, которая ссылается на boost, "запоминая" окружение speed_booster
f = speed_booster()

# Вызываем f с аргументом 100: это вызывает boost(100)
print(f(100))  # Вывод: 110.0 (100 * 1.1 = 110.0, округление до двух знаков)