'''Считай данные в таком порядке:

    целое число --> количество элементов;
    для каждого элемента три строки:
        название элемента (строка),
        символ элемента (строка),
        атомный номер (целое число);
    затем одна строка --> символ элемента для поиска.

Выведи:
    полный список элементов в формате (с порядковым номером по вводу),
    пустую строку,
    результат поиска по символу.
'''

# Создание пустых списков для хранения: названий, символов и атомных номеров элементов
element, symbol, weight = [], [], []

# Переменная для подсчёта количества элементов (можно было использовать i, но здесь используется count)
count = 0

# Вывод заголовка списка элементов
print('Список элементов:')

# Чтение количества элементов и цикл по этому числу
for i in range(int(input())):
    # Считываем и сохраняем название элемента
    element.append(input())
    # Считываем и сохраняем символ элемента
    symbol.append(input())
    # Считываем и сохраняем атомный номер (преобразуем в целое число)
    weight.append(int(input()))
    # Увеличиваем счётчик элементов на 1
    count += 1
    # Выводим информацию об элементе в требуемом формате
    print(f'{count}. {element[i]} ({symbol[i]}) - атомный номер: {weight[i]}')

# Печатаем пустую строку (как требуется по условию)
print()

# Считываем символ элемента, который нужно найти
new_sym = input()

# Проверяем, есть ли введённый символ в списке symbol
# Если есть — выводим атомный номер этого элемента
# Если нет — сообщаем, что элемент не найден
print(f'Атомный номер элемента {new_sym}: {weight[symbol.index(new_sym)]}'
      if new_sym in symbol
      else f'Элемент с символом {new_sym} не найден.')
